                <h2>Biografía resumida</h2>
                <p>El <b>Dr. Luis Miguel Dorantes Álvarez</b> es egresado de la facultad de medicina de la UNAM. Realizo su internado de posgrado en el desaparecido Hospital Colonial y después ingreso al <u><a href="http://himfg.com.mx/interior/dir_ensenanza.html">Hospital Infantil de México Federico Gómez</a></u>                    donde laboro por los siguientes 30 años. Primero realizo la residencia en Pediatría, luego la especialidad en Endocrinología Pediátrica, ahí decidió extender sus conocimientos en el extranjero y permaneció 2 años en la <u><a href="https://www.mayoclinic.org/es-es">Clínica Mayo en Rochester Minnesota</a></u>                    como “fellow” en <b>investigación</b> sobre el metabolismo mineral en niño, a su regreso al Hospital Infantil de México se incorporó al <b>Departamento de Endocrinología</b> como <b>médico base</b> e <b> investigador.</b> En 1983 tomo
                    la <b>jefatura</b> del Departamento de Endocrinología y actualmente funge como <b>Jefe de Servicio de Endocrinología del mismo hospital.</b></p>
                <p>Durante su jefatura fundo <b> la Clínica de Atención del niño diabético</b>, <b> la Clínica de problemas intersexuales</b> y <b> la Clínica de maduración sexual precoz</b>, que continúan funcionando en el hospital. </p>
                <p>Recién cumplo 35 años como <b>profesor titular</b> del curso de especialidad en Endocrinología Pediátrica, curso de residencia donde se han formado 58 Endocrinólogos Pediátricos mexicanos y de varios países de Latinoamérica. Siendo una
                    de las especialidades más solicitadas por el ambiente académico de alto nivel, de respeto, ética y agradable estancia.</p>
                <p>Al Dr. Luis Miguel Dorantes Álvarez lo caracteriza por sobre todo su amor por los niños, la buena relación que establece con ellos y su interés por ayudarlos sin dañarlos.</p>
