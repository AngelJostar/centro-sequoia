                <h2>Biografía resumida</h2>
                <p>La Dra. América Liliana Miranda Lora es especialista en Endocrinóloga Pediátrica, egresada de la Facultad de Medicina de la Universidad Nacional Autónoma de México con un promedio de 9.57. Tiene especialidad en pediatría y médico cirujano por la misma universidad. Posteriormente realizo una alta especialidad médica en diabetes en niños. Esta Certificada por el Consejo Mexicano de Endocrinología y reconocida como una de las mejores especialistas en Endocrinología Pediátrica de la Ciudad de mexica. 
                Actualmente es Jefa de Investigación de Medicina Basada en Evidencias del Hospital Infantil de mexica Federico Gómez, y médico de atención en Centro Sequoia. 
                Hasta el día de hoy ha realizado 66 estudios de investigación en las áreas de crecimiento infantil, pubertad precoz, edad ósea, obesidad, diabetes y leucemia. 
