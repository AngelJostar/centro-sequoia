        <h2 id="1">¿Qué es la adolescencia tardia?</h2>
        <p>Se llama <strong>adolescencia o pubertad tardía</strong> cuando la edad de inicio de la pubertad es más tarde de lo habitual. Sucede cuando los <a href="/aprende-del-crecimiento-infantil/etapas-del-desarollo-de-tanner/">cambios físicos</a>  de madurez sexual no aparecen en las <strong>mujeres</strong> a los 12 años, ni en los <strong>hombres</strong> a los 14 años. Estos cambios principalmente son el crecimiento de los senos o testículos, el vello púbico y los cambios en la voz (estos se conocen como características sexuales secundarias).</p>
        <p><a href="/cambios-en-la-adolescencia/">La adolescencia</a> es el momento en el que los niños se convierten en adultos. Tú y su hijo sabrán que él está atravesando la <strong>adolescencia</strong> por la forma en que <a href="/aprende-del-crecimiento-infantil/etapas-del-desarollo-de-tanner/">cambia su cuerpo</a>.</p>
        <p>Por lo general, estos cambios comienzan entre los <strong>8 y los 14 años</strong> para las mujeres y entre los <strong>9 y los 15 años</strong> para los hombres. Este es un amplio rango de edades, y es por eso que los niños y niñas pueden desarrollarse varios años antes o después que el promedio de sus compañeros de clase.</p>
        <p>Los <a href="/especialistas-en-crecimiento">médicos</a>  pueden ayudar a los adolescentes con <strong>adolescencia tardía</strong> a desarrollarse para que su crecimiento se ponga al día con el de sus compañeros.</p>
        <h2 id="2">Características</h2>
        <p>La falta de signos de adolescencia es el indicador principal de que un niño puede tener adolescencia tardía. Cada niño puede experimentar los <strong>síntomas</strong> de manera diferente.</p>
        <p>Los <strong>síntomas</strong> pueden parecerse a los de otros problemas o afecciones médicas. Siempre consulte al médico de su hijo para obtener un diagnóstico.</p>
        <div class="size-img-resposive">
            <img src="/img/generalidades-del-crecimiento-infantil/H-Adolecencia-Tardia.webp" alt="Cuando se considera pubertad tardia">
