        <h2 id="5">Diagnostico</h2>
        <p>El <strong>diagnóstico</strong> de la <strong>adolescencia tardía</strong> puede incluir:</p>
        <ol>
            <li><strong>Historia Clínica</strong>. Se deben evaluar antecedentes de quimioterapia, radiación, valoración del estado puberal, <a href="/aprende-del-crecimiento-infantil/edad-osea">edad ósea</a>  y en las niñas exploración ginecológica y ecográfica de la pelvis.</li>
            <li><strong>Examen físico</strong>. Se hace una exploración física general, antropométrica, evolución del <a href="/aprende-del-crecimiento-infantil//etapas-del-desarollo-de-tanner">crecimiento y de los genitales</a>. </li>
            <li><strong>Análisis de sangre</strong>. Estos se realizan para controlar los niveles hormonales, buscar problemas cromosómicos y detectar <a href="/factores-que-perjudican-el-crecimiento/padecimientos-que-afectan-el-crecimiento">trastornos crónicos</a> que puedan retrasar la pubertad. Estos pueden incluir diabetes o anemia.</li>
            <li><strong>Radiografía</strong>. Se puede realizar una radiografía de la mano y la muñeca izquierdas. Esto puede estimar la <a href="/aprende-del-crecimiento-infantil/edad-osea">edad ósea</a> de su hijo. Con la pubertad precoz, la edad ósea suele ser mayor que la edad calendario. La pubertad precoz significa que el cuerpo de su hijo comienza a cambiar al de un adulto demasiado pronto. Este cambio ocurriría antes de los 8 años en las niñas y antes de los 9 años en los niños.</li>
            <li><strong>Tomografía computarizada</strong>. Esta prueba utiliza una serie de radiografías y una computadora para tomar imágenes detalladas del cuerpo. Una tomografía computarizada puede mostrar huesos, músculos, grasa y órganos. Las tomografías computarizadas son más detalladas que las radiografías regulares.</li>
            <li><strong>Resonancia magnética</strong>. Esta prueba utiliza imanes grandes y una computadora para generar imágenes detalladas de los tejidos del cuerpo.</li>
        </ol>
        <h2 id="6">Tratamiento</h2>
        <p>El <a href="/especialistas-en-crecimiento">médico especialista</a> de su hijo considerará su edad, su estado general de salud y otros factores al recomendar el tratamiento.</p>
        <p>El tratamiento para la <a href="">pubertad tardía</a> depende de la causa del problema. En muchos casos, cuando se trata la causa, se resuelve el problema. Si la pubertad tardía se hereda, generalmente no se necesita tratamiento. En algunos casos, el tratamiento se puede realizar con <a href="/aprende-del-crecimiento-infantil/hormona-del-crecimiento">terapia hormonal</a>. Esto ayuda a que se produzcan características sexuales secundarias.</p>
        <h2 id="7">Consecuencias</h2>
        <p>Puede ser difícil ver <strong>crecer y desarrollarse</strong> a tus amigos cuando no te está sucediendo lo mismo. Tus hijos pueden sentir que nunca se va a poner al día. Incluso cuando el médico te asegura que todo estará bien.</p>
        <p>La adolescencia tardía puede ser difícil de aceptar y manejar para cualquier persona. Pero es un problema que generalmente se resuelve. Pida ayuda si tiene alguna inquietud sobre el <a href="/etapas-del-desarrollo-humano">desarrollo de tus hijos</a>.</p>
        <p>La mayoría de los <a href="/cambios-en-la-adolescencia">adolescentes</a> con pubertad tardía se desarrollarán con el tiempo normalmente y no tendrán problemas continuos. Algunas causas necesitarán tratamiento con hormonas. El apoyo emocional puede ayudar a los adolescentes a lidiar con este problema.</p>
        <h3 id="7.1">¿La adolescencia tardía afecta el crecimiento?</h3>
        <p>Si. Los niños con adolescencia tardía tienden a no crecer tanto durante sus períodos de <a href="/aprende-del-crecimiento-infantil">crecimiento acelerado o estirón puberal</a> como otros niños, lo que puede hacerlos un poco más pequeños cuando son adultos.</p>
