        <h3 id="2.1">Síntomas en niñas mujeres (H3)</h3>
        <p>La <strong>adolescencia</strong> tardía en las <strong>mujeres</strong> ocurre cuando los senos no se desarrollan a los 13 años o los períodos menstruales no comienzan a los 16 años.</p>
        <p>Los <a href="/aprende-del-crecimiento-infantil/etapas-del-desarollo-de-tanner">cambios en la pubertad</a> ocurren cuando el cuerpo comienza a producir hormonas sexuales. Estos cambios normalmente comienzan a aparecer en niñas de entre 8 y 14 años.</p>
        <p>Con la <strong>pubertad tardía</strong>, estos cambios no ocurren o, si ocurren, no progresan normalmente: </p>
        <ul>
            <li>Poco desarrollo de senos</li>
            <li>Falta de crecimiento de vello púbico</li>
            <li>No hay estirón de crecimiento </li>
            <li>Falta de período (menstruación)</li>
            <li>No hay cambio en la forma del cuerpo (caderas más anchas)</li>
        </ul>
        <h3 id="2.2">Síntomas en niños hombres</h3>
        <p>La <strong>adolescencia tardía</strong> es más común en los hombres que en las mujeres. En los niños es cuando la pubertad no comienza a los <strong>14 años de edad</strong> y estos cambios no ocurren o no progresan normalmente.</p>
        <ul>
            <li> No crece vello púbico y facial </li>
            <li>No tienen un estirón puberal </li>
            <li>Sus testículos y pene no se agrandan </li>
            <li>La forma del cuerpo no cambia </li>
            <li>Los hombros no se ensanchan</li>
            <li>El cuerpo no se vuelve más musculoso</li>
        </ul>
