        <h2 id="3">Causas </h2>
        <p>La <strong>adolescencia tardía</strong> con mayor frecuencia no tiene una <strong>causa</strong> conocida. En algunos casos, puede ser hereditario. En otros casos, puede ser causado por cualquiera de estos:</p>
        <div class="tamaño-img">
            <img src="/img/generalidades-del-crecimiento-infantil/Banner-Pubertad-Retrasada.webp" alt="Banner pubertad atrasada">
        </div>
        <ul>
            <li>Demasiado ejercicio.</li>
            <li>Falta severa de comer (anorexia).</li>
            <li>Enfermedad crónica.</li>
            <li>Tumores de la glándula pituitaria o del hipotálamo.</li>
            <li>Hipófisis hipoactiva (hipopituitarismo).</li>
            <li>Tiroides hipoactiva (hipotiroidismo).</li>
            <li>Desarrollo anormal del sistema reproductivo.</li>
            <li>Problemas cromosómicos.</li>
            <li>Trastorno genético.</li>
            <li>Incapacidad del cuerpo para utilizar hormonas andrógenas.</li>
        </ul>
        <h2 id="4">Clasificación según su causa</h2>
        <h3>Retraso constitucional del crecimiento y desarrollo (RCCD).</h3>
        <p>Los niños con <a href="/padecimentos-que-afectan-el-crecimiento/retraso-constitucional-del-crecimiento-y-la-pubertad">retraso constitucional del crecimiento</a> (CGD), la causa más común de baja estatura y <strong>retraso puberal</strong>, típicamente tienen un crecimiento lineal retardado dentro de los primeros 3 años de vida. </p>
        <p>La velocidad de crecimiento lineal y el aumento de peso se ralentizan a partir de los 3 a 6 meses de edad, lo que resulta en un cruce descendente de los percentiles de estatura, que a menudo continúa hasta los 2 a 3 años. En ese momento, el aumento de estatuea se reanuda a un ritmo normal y estos niños crecen a lo largo de los <a href="/aprende-del-crecimiento-infantil/graficas-de-crecimiento">percentiles de crecimiento</a> más bajos o por debajo de la curva, pero paralelamente a ella durante el resto de los años prepúberes.</p>
        <p>En estos casos, por lo general la <a href="/aprende-del-crecimiento-infantil/edad-osea">edad ósea</a> es inferior a la cronológica. La mayoría de los varones con <a href="/padecimentos-que-afectan-el-crecimiento/retraso-constitucional-del-crecimiento-y-la-pubertad">retraso constitucional del crecimiento (CGD)</a> inician la pubertad cuando alcanzan una edad ósea entre 12 y 14 años y las niñas cuando está entre 11 y 13 años. A pesar del retraso del estirón puberal, generalmente la talla se recupera, aunque hay un pequeño porcentaje de pacientes que alcanza una talla definitiva inferior a la prevista.</p>
        <h3>Adolescencia tardía secundaria a enfermedades crónicas</h3>
        <p>Se desconoce la incidencia exacta de la adolescencia tardía asociada a enfermedades crónicas. Sin embargo prácticamente todos los niños con cualquier <a href="/factores-que-perjudican-el-crecimiento/padecimientos-que-afectan-el-crecimiento">enfermedad crónica</a> (infecciones recurrentes, inmunodeficiencia, enfermedad gastrointestinal, alteraciones renales, enfermedades respiratorias, anemia crónica, enfermedad endocrina, trastornos alimentarios y una serie de anomalías diversas) pueden presentar esta situación. </p>
        <p>El retraso puberal asociado con <strong>enfermedades crónicas</strong> se acompaña de un retraso en el estirón de <a href="/aprende-del-crecimiento-infantil/estiron-puberal">crecimiento puberal</a>. El grado en el que el crecimiento y el desarrollo puberal se ven afectados en las enfermedades crónicas depende del tipo de enfermedad y de los factores individuales, así como de la edad de inicio de la enfermedad, su duración y gravedad. </p>        
        <p>Cuanto antes su aparición y cuanto más prolongada y grave sea la enfermedad, mayores serán las repercusiones sobre el crecimiento y el desarrollo puberal. </p>
        <h3>Hipogonadismo hipergonadotrópico</h3>
        <p>El hipogonadismo es una afección en la que los testículos masculinos o los ovarios femeninos producen pocas hormonas sexuales o ninguna. La mayoría de los pacientes con hipogonadismo son hombres. Esta afección se debe a un problema con la glándula pituitaria o el hipotálamo.</p>
