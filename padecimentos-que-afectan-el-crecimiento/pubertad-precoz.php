        <h3>¿A qué edad se considera pubertad precoz?</h3>
        <p>La <strong>pubertad precoz</strong>, también llamada desarrollo precoz, es cuando:</p>
        <ul>
            <li>las niñas desarrollan signos de pubertad antes de los 8 años de edad</li>
            <li>los niños desarrollan signos de pubertad antes de los 9 años de edad</li>
        </ul>
        <p>Algunos <strong>niños y niñas</strong> pueden desarrollar solo algunos signos de pubertad a una edad temprana, pero no todos los signos, es por eso que tienes que estar al pendiente del desarrollo de tus hijos.</p>
        <p>Por ejemplo, las niñas pueden comenzar la menstruación antes de los 8 años, pero no tener desarrollo de los senos. Algunas niñas muy pequeñas (generalmente de 6 meses a 3 años) pueden mostrar un <strong>desarrollo mamario</strong> que luego desaparece o puede durar pero sin otros cambios físicos de la pubertad. Esto se llama <strong>telarquia prematura</strong> y generalmente no causa problemas duraderos.</p>
        <p>De forma parecida, algunos niños y niñas pueden sufrir un crecimiento temprano del vello púbico o de las axilas u olor corporal que no está vinculado con otros cambios en el desarrollo sexual. Esto se llama <strong>adrenarquia prematura</strong>.</p>
        <p>Es posible que estos niños necesiten ver a su médico para descartar una pubertad precoz "verdadera". sin embargo la mayoría no necesita tratamiento y presentará los otros signos esperados de la pubertad a la edad habitual.</p>
        <p>Consulte a su médico si esto le sucede a su hijo.</p>
