        <h3>¿Por qué se considera una alteración la pubertad precoz?</h3>
        <p>Por muchas razones:</p>
        <ol>
            <li>Por su efecto negativo sobre la estatura final ya que son niñas o niños que se desarrollan muy rápido y que terminan de crecer pronto. </li>
        <li>Porque el aspecto psicológicos <!-- <a href="">psicológico</a> --> que ocasiona que una niña de menos de 8 años empiece a tener manifestaciones de <a href="/aprende-del-crecimiento-infantil/estiron-puberal">desarrollo puberal</a>, estas niñas tienden a acercarse a niñas mayores a tener pensamientos diferentes de las demás niñas, dejan de ser niñas y eso no es bueno. </li>
            <li>Porque la pubertad precoz sobre todo en niñas condiciona frecuentemente alteraciones posteriores que incluyen alteraciones en el ritmo menstrual, disminución en la fertilidad y menopausias más tempranas. </li>
        </ol>
        <p>Entonces por estas razones no es conveniente dejar que una niña o un niño tengan un desarrollo sexual tan precoz. </p>
        <h2 id="2">Causas la pubertad precoz</h2>
        <p>Para comprender qué <strong>causa</strong> la <strong>pubertad precoz</strong> en algunos niños, es útil saber qué causa el comienzo de la <strong>pubertad</strong>. </p>
        <p>Para iniciar la adolescencia, el cerebro comienza a producir una hormona llamada <strong>hormona</strong> liberadora de gonadotropina. Cuando esta hormona llega a la <strong>glándula pituitaria</strong> (una pequeña glándula con forma de frijol en la base de su cerebro) fomenta la producción de más hormonas en los <strong>ovarios</strong> de las <strong>mujeres</strong> (estrógeno) y en los <strong>testículos</strong> de los <strong>hombres</strong> (testosterona).</p>
        <p>El <strong>estrógeno</strong> está relacionado en el crecimiento y desarrollo de las <strong>características sexuales femeninas</strong>. La testosterona es encargada del crecimiento y desarrollo de las <strong>características sexuales masculinas</strong>.</p>
        <p>La razón por la que este proceso comienza temprano en algunos niños depende de si tienen pubertad <strong>precoz central</strong> o <strong>pubertad precoz periférica</strong>.</p>
        <h3 id="2.1">Pubertad precoz central</h3>
        <p>La causa de este tipo de pubertad precoz a menudo resulta imposible de identificar.</p>
        <p>En la pubertad <strong>precoz central</strong>, el proceso de la pubertad comienza demasiado pronto. Por lo demás, el patrón y la sincronización de los pasos del proceso son normales. Para la mayoría de los niños con esta afección, no existe un problema médico subyacente ni una razón identificable para la pubertad temprana.</p>
        <p>En casos raros, la pubertad precoz central puede ser causada por:</p>
        <ul>
            <li>Un tumor en el cerebro o la médula espinal (sistema nervioso central)</li>
            <li>Un defecto en el cerebro presente al nacer, como acumulación excesiva de líquido (hidrocefalia) o un tumor no canceroso (hamartoma)</li>
            <li>Radiación al cerebro o la médula espinal</li>
            <li>Lesión en el cerebro o la médula espinal.</li>
            <li>Síndrome de McCune-Albright: una enfermedad genética rara que afecta los huesos y el color de la piel y causa problemas hormonales.</li>
            <li>Hiperplasia suprarrenal congénita: un grupo de trastornos genéticos que involucran la producción anormal de hormonas por las glándulas suprarrenales.</li>
            <li>Hipotiroidismo: Es un trastorno en el cual la glándula tiroides no produce la cantidad requerida de ciertas hormonas importantes</li>
        </ul>
        <h3 id="2.2">Pubertad precoz periférica</h3>
        <p>El estrógeno o la testosterona en el cuerpo de su hijo provocan este tipo de pubertad precoz.</p>        
        <p>La pubertad <strong>precoz periférica</strong> menos común se da sin la participación de la hormona en el cerebro (GnRH) que normalmente desencadena el comienzo de la pubertad. Por el contrario, la causa es la liberación de estrógeno o testosterona en el cuerpo ocasionado a problemas con los ovarios, los testículos, las glándulas suprarrenales o la glándula pituitaria.</p>
        <p>Tanto en niñas como en niños, lo siguiente puede conducir a una pubertad precoz periférica:</p>
        <ul>
        <li>Un tumor en las glándulas suprarrenales o en la glándula pituitaria que libera estrógeno o testosterona.</li>
        <li>Síndrome de McCune-Albright, un trastorno genético poco común que afecta el color de la piel y los huesos y causa problemas hormonales.</li>
        <li>Exposición a fuentes externas de estrógeno o testosterona, como pueden ser cremas o ungüentos.</li>
        </ul>
        <p>En las niñas, la pubertad precoz periférica también puede estar relacionada con:</p>
        <ul>
            <li>Quistes ováricos</li> 
            <li>Tumores de ovario</li> 
        </ul>
        <p>En los niños, la pubertad precoz periférica también puede deberse a:</p>
        <ul>
            <li>Un tumor en las células que producen los espermatozoides (células germinales) o en las células que producen testosterona (células de Leydig).</li> 
            <li>Un trastorno poco común llamado precocidad sexual familiar independiente de las gonadotropinas, que es causado por un defecto en un gen, puede resultar en la producción temprana de testosterona en los niños, generalmente entre las edades de 1 y 4 años.</li> 
        </ul>
        <h2 id="3">Signos y síntomas de la pubertad precoz</h2>
        <p><strong>Signos y síntomas</strong> asociados a la pubertad precoz son los siguientes:</p>
        <ul>
            <li>Crecimiento de los senos y primer período menstrual en las niñas</li> 
            <li>Agrandamiento de los testículos y el pene, aparición vello facial y engrosamiento de la voz en los niños</li> 
            <li>Vello púbico o en las axilas</li> 
            <li>Crecimiento acelerado</li> 
            <li>Acné</li> 
            <li>Olor – sudor</li> 
        </ul>
