        <h2 id="1">¿Qué es la pubertad precoz?</h2>
        <p>La <strong>pubertad precoz</strong> es la aparición de características sexuales <!-- <a href="">características sexuales</a> --> en <strong>niñas</strong> menores de 8 años o en <strong>niños</strong> menores de 9 años.</p>
        <p>El <strong>desarrollo precoz</strong> es una <strong>alteración</strong> que puede ocurrir en <strong>niños o en niñas</strong>, pero es mucho más frecuente en niñas (la pubertad precoz es menos común en los niños) . Actualmente es extraordinaria la cantidad de niñas que inician su <strong>desarrollo</strong> en forma <strong>precoz</strong>. </p>
        <p>Esta situación tiene mucho que ver con la <a href="/factores-que-influyen-en-el-crecimiento">herencia</a>. Hay niñas y niños que pueden pertenecer a familias que tienden a <strong>desarrollarse tempranamente</strong>. Hoy en día, los <a href="/especialistas-en-crecimiento">especialistas</a> pensamos que también tiene que ver con nuestros hábitos de alimentación que pueden proveer substancias que influyen en el proceso del <strong>desarrollo sexual</strong>.</p>
