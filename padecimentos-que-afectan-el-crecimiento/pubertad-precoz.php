        <h2 id="4">Diagnostico</h2>
        <p>Para diagnosticar la pubertad precoz, el médico:</p>        
        <ul>
            <li>Debera Revisar el historial clínico del niño y de su familia</li> 
            <li>Hará un examen físico</li> 
            <li>Hará análisis de sangre para medir los niveles hormonales</li> 
        </ul>
        <p>Las radiografías de las manos y muñecas también son claves para diagnosticar la pubertad precoz en las niñas y los niños. Las radiografías pueden ayudar al médico a determinar la edad aproximada de los huesos del niño o niña, lo cual muestra si los huesos están alcanzando su madurez muy rápido.</p>
        <h2 id="5">Tratamiento</h2>
        <p>El principal objetivo del tratamiento es permitir que el niño crezca hasta alcanzar la estatura normal de un adulto.</p>
        <p>El tratamiento para la pubertad precoz depende de la causa. No obstante, en algunos casos, no hay una causa aparentemente identificable para la pubertad precoz. En este caso, es posible que tu hijo no requiera tratamiento dependiendo de su edad y de la velocidad con que avance la pubertad. Cabe la posibilidad que el médico de tu hijo quiera vigilarlo durante un par meses para ver cómo se está desarrollando.</p>
        <p>La mayoría de los infantes con pubertad precoz, en la que no hay una enfermedad subyacente, se pueden tratar de manera eficiente con medicamentos. Este tratamiento, llamado "terapia con análogos de GnRH", suele consistir en una inyección mensual de un medicamento que retrasa el desarrollo y les da tiempo de poder alcanzar su estatura ideal de adulto.</p>        
        <h2 id="6">Factores de riesgo</h2>
        <p>Factores que aumentan el riesgo de pubertad precoz incluyen:</p>
        <ul>
            <li><strong>Ser niña.</strong> Las niñas tienen muchas más probabilidades de desarrollar una pubertad precoz.</li>
            <li><strong>Ser obeso.</strong> Los niños que tienen un sobrepeso significativo tienen un mayor riesgo de desarrollar una pubertad precoz.</li>
            <li><strong>Estar expuesto a hormonas sexuales.</strong> Entrar en contacto con una crema o ungüento de estrógeno o testosterona, u otras sustancias que contienen estas hormonas (como un medicamento para adultos o suplementos dietéticos), puede aumentar el riesgo de que su hijo padezca de una pubertad precoz.</li>
            <li><strong>Tener otras condiciones médicas.</strong> La pubertad precoz puede ser una complicación del síndrome de McCune-Albright o la hiperplasia suprarrenal congénita, afecciones que involucran la producción anormal de hormonas masculinas (andrógenos). En casos raros, la pubertad precoz también puede estar relacionada con hipotiroidismo.</li>
            <li><strong>Haber recibido radioterapia del sistema nervioso central.</strong> El tratamiento con radiación para tumores, leucemia u otras afecciones puede aumentar el riesgo de pubertad precoz.</li>
        </ul>
