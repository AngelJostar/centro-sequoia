        <h3>¿Cómo afecta a los niños y niñas?</h3>
        <p>Cuando terminan la pubertad, las niñas <strong>dejaran de crecer</strong>. </p>        
        <p>Los niños con pubertad temprana no tratada a veces no alcanzan su <a href="/estatura">estatura adulta</a>completa, porque sus huesos maduran y el <a href="/aprende-del-crecimiento-infantil/edad-osea">crecimiento óseo</a> se detiene demasiado pronto. </p>
        <p>A pesar de que pueden tener un brote de crecimiento temprano que los hace más altos que otros niños de su edad, terminar la pubertad temprano hace que dejen de crecer demasiado pronto. </p>        
        <p>La pubertad temprana puede ser <a href="/cambios-en-la-adolescencia">difícil para su hijo de lidiar emocional y socialmente</a>. Las niñas pueden sentirse confundidas o avergonzadas por tener sus períodos menstruales y senos antes que otros niños. Pueden estar de mal humor e irritables. Una de las partes más difíciles son las burlas que a veces enfrentan estos niños por parte de los demás.</p>
