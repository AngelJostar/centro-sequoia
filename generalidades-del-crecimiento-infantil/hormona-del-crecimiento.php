                <h2><b>¿Cómo crecemos? </b></h2>
            </div>
            <div class="justificar-texto-85 justificar-derecha-texto pt-15 ">
                <p>El proceso de crecimiento comienza con la producción de hormona de crecimiento en una parte dentro de la cabeza que se llama “glándula pituitaria”. Otra glándula que se llama “hipotálamo”, le indica a la glándula pituitaria cuándo debe liberar hormona de crecimiento dentro de la sangre o “torrente sanguíneo”.</p>
                <p>Para ello el hipotálamo primero produce una hormona llamada “hormona liberadora de hormona de crecimiento”. Esta hormona estimuladora viaja a través de venas pequeñitas directo a la glándula pituitaria y provoca que libere un poco de hormona de crecimiento.</p>
                <p>La hormona de crecimiento viaja a través del cuerpo a otro conjunto de venas pequeñitas para entrar en el torrente sanguíneo. Así puede viajar a todas las partes del cuerpo, incluyendo al hígado que también juega un papel importante en el crecimiento.</p>
                <p>La hormona entra en el hígado causando que se libere otra hormona llamada “factor de crecimiento parecido a la insulina-1” (IGF-1). Esta hormona sale del hígado y entra también al torrente sanguíneo.</p>
                <p>Juntos, la hormona de crecimiento y el IGF-1, viajan a través del torrente sanguíneo a todas las partes del cuerpo, incluyendo los huesos largos de las piernas que son los que tienen el mayor efecto en nuestra estatura.</p>
