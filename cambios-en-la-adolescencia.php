        <h2>Cambios físicos</h2>
        <p>Comprendida como la culminación del desarrollo <strong>físico y sexual</strong>. Esta madurez se relaciona con la llegada a la pubertad. Los cambios biológicos se utilizan como criterio de inicio a la adolescencia. Esta etapa también puede entenderse como el proceso de maduración que conduce a la <strong>madurez sexual</strong>, por lo tanto, la <strong>capacidad de reproducirse</strong>.</p>
        <h2>Cambios psicológicos</h2>
        <p>Caracterizada por la <strong>reorganización de la identidad</strong> la cual implica un nuevo compromiso con un conjunto de calores y la adopción de una actitud frente a la sociedad, este cambio esta presente a lo largo de toda la adolescencia.</p>
        <h2>Cambios emocionales </h2>
        <p>El adolescente tiende a sufrir de <strong>cambios de humor</strong> extremos. La inseguridad se vuelve una emoción muy marcada por lo que tienden a ser bastante susceptibles a la crítica. En esta etapa los adolescentes se suelen centrarse en si mismos, teniendo la idea de que son la única persona que se siente como ellos, dando lugar a las típicas ideas de que .</p>
        <h2>Cambios cognitivos </h2>
        <p>Estos <strong>cambios</strong> se reflejan en la <strong>mentalidad</strong>. El adolescente se vuelve capaz de pensar de manera abstracta y mantener una actitud <strong>crítica y reflexiva</strong>.</p>
        <h2>Cambios sociales</h2>
        <p>Esta etapa consiste en la <strong>búsqueda de la autonomía e independencia</strong>, suele ser habitual que en esta etapa los adolescentes cuestionen la autoridad de personas adultas, que busquen el tener mas amistades y pertenecer a un grupo social de adolescentes similares a ellos. Las relaciones con las personas adultas cambian, en ocasiones radicalmente, principalmente con los padres, sin embargo, no siempre tiene que ser tan abrupto el cambio, cada persona atraviesa la adolescencia de diferente manera.</p>
        <h2>Cambios hormonales en la adolescencia</h2>
        <p>Las hormonas en los adolescentes traen consigo una <strong>gran cantidad de cambios</strong>, tanto en el físico como en el estado de ánimo. Estos cambios son causados por las llamadas hormonas sexuales: <strong>andrógenos</strong>, <strong>estrógenos</strong> y <strong>progesterona</strong>, sin las cuales no podrían llevarse acabo cambios en el <strong>desarrollo físico y sexual</strong>, así como afectar en gran medida el estado de ánimo y las emociones en adolescentes.</p>
