                <h2 class="box-titles-h2">Factores Endógenos (Propios del niño):</h2>
            </div>
            <ul>
                <li><strong>Factores genéticos del crecimiento</strong>: Existen genes en el cuerpo humano que participan en el crecimiento de un niño y que son heredados de ambos padres . Los genes del crecimiento de los papás determinan el 80 % de la talla final adulta de su hijo.</li>
                <li><strong>Factores hormonales del crecimiento</strong>: Durante la etapa fetal interactúan genes y hormonas producidas por el bebe y la placenta para cumplir la función del crecimiento y después del nacimiento la hormona de crecimiento y las hormonas tiroideas realizarán esta función. En la pubertad las hormonas sexuales (que son las que dan los cambios de la adolescencia) se suman a la función del crecimiento para culminar en la talla final adulta.</li>
            </ul>
            <div class="box-titles">
                <h2 class="box-titles-h2">Factores Exógenos (Externos):</h2>
            </div>
            <ul>
                <li><strong>La alimentación correcta</strong> del niño aporta el material energético necesario para su crecimiento. La desnutrición o enfermedades que no permitan la absorción adecuada de los nutrientes pueden dar como resultado una talla baja.</li>
                <li><strong>Hábitos:</strong> La práctica regular del ejercicio para niños es beneficioso para el crecimiento, no así el de competición o alto rendimiento. El sueño durante la noche y de manera temprana es otro factor que influye en la producción de la hormona de crecimiento natural.</li>
                <li><strong>Psicológicos y afectivos.</strong> Alguna privación emocional, anorexia y depresión pueden provocar alteraciones en el crecimiento.</li>
            </ul>
