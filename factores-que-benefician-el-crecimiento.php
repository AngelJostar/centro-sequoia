            <p>La <strong>niñez</strong> es una etapa de grandes cambios, tanto <strong>físicos</strong> como <strong>psicológicos</strong>, los cuales ocurren lentamente si lo vemos desde la perspectiva de un niño, o son efimeros si lo comparamos con la vida promedio de un ser humano. El <strong>desarrollo infantil</strong>
                es muy diferente unos de otros, algunos suelen niños tardar mas que otros en desarrollarse completamente, mientras que otros debido a circunstancias que veremos mas adelante son afortunados de tener un periodo de <strong>desarrollo saludable</strong>. A continuación 
            te presentamos un listado con los principales <strong>factores</strong> que pueden beneficiar el <strong>desarrollo infantil</strong>.</p>
            <h2>1.- Herencia</h2>
            <p>La herencia consiste en la transmisión de <strong>características físicas</strong> de padres a hijos a mediante sus genes. Influye en todos los aspectos de la apariencia física, como la altura, el peso, la estructura corporal, la textura del cabello, el color de los ojos e incluso la inteligencia y las aptitudes. Existen enfermedades y afecciones que tambien pueden transmitirse a través de los genes lo que afectan negativamente al crecimiento y desarrollo del niño.</p>
            <h2>2.- Medio ambiente</h2>
            <p>El medio ambiente juega un papel super importante en el desarrollo de los niños y representa la suma total de estimulación física y psicológica que recibe el niño. Algunos de los factores ambientales que influyen en el desarrollo de la primera infancia incluyen el entorno físico y las condiciones geográficas del lugar en el que vive el niño, así como su entorno social y las relaciones con la familia y los compañeros.</p>
            <h2>3.- Sexo</h2>
            <p>El sexo es otro factor a considerar que afecta el crecimiento físico y el desarrollo de un niño. Los niños y las niñas crecen de maneras distintas, especialmente cuando se acercan a la pubertad. Los niños tienen tendencia a ser más altos y físicamente más fuertes que las niñas. Sin embargo, las niñas suelen madurar más rápido durante la adolescencia, por otra parte los niños maduran durante un período de tiempo más prolongado. </p>
            <h2>4.- Ejercicio y salud </h2>
            <p>Cuando se habla de ejercicio no significa necesariamente que los niños practiquen alguna actividad fisica. El ejercicio puede ser cualquier actividad que involucre movimiento, ya sea el juego normal o simplemente correr o caminar con regularidad, sin embargo esta claro que realizar alguna actividad deportiva siempre traera beneficios al cuerpo, como ganar fuerza muscular y ganar masa ósea. El ejercicio ayuda a los niños a crecer bien y fortalece su sistema inmunológico</p>
