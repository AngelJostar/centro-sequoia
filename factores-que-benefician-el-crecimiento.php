            <h2>5.- Nutrición</h2>
            <p>La nutrición es un factor crucial en el crecimiento y desarrollo, ya que todo lo que el cuerpo requiere para formarse y repararse proviene de los alimentos que ingerimos. La desnutrición puede llegar a causar enfermedades que afecten perjudicialmente al crecimiento y desarrollo de los niños. sin embargo, comer de más puede provocar obesidad y problemas de salud a largo plazo, como diabetes y enfermedades cardíacas. Una dieta balanceada en vitaminas, minerales, proteínas , carbohidratos y grasas es esencial para el desarrollo del cerebro y del cuerpo.</p>
            <h2>6.- Influencia familiar</h2>
            <p>La familias tiene el mayor impacto en la crianza de un niño y determinan las formas en que se desarrollan psicológica y socialmente. Tener padres que procuren y se preocupen por desarrollar actividades beneficas para su hijo, tendran un mejor desarrollo que niños que sufran de abusos o descuido de parte de sus familiares</p>
            <h2>7.- Influencias geográficas</h2>
            <p>El lugar donde vivemos también tiene un gran peso en el desarrollo humano. Vivir en lugares donde no se fomenten actividades sociales ni educativas asi como vivir en lugares donde el clima de un lugar afecte fisicamente la salud de los niños, puede provocar un mal desarrollo del ser humano.  </p>
            <h2>8.- Sueño</h2>
            <p>El descanso nocturno resulta imprescindible para un buen crecimiento. Según indican los endocrinologos, entre el 70% y el 80% de la hormona del crecimiento es secretada durante el sueño. Es importante que los niños no interrumpan sus horas de sueño y tampoco las reduzcan ya que el ciclo de la hormona no se libera adecuadamente si esto se presenta. </p>

        
