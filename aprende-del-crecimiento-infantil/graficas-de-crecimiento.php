        <h2 id="1">Definiciones</h2>
        <ol>
            <li><strong>Tabla de peso y talla:</strong> Son comparaciones de datos donde encontrarás la relación estatura-peso que debe medir y pesar tu hijo. Estas evalúan, el estado nutricional y la estatura lineal del niño o la niña.</li>
            <li><strong>Graficas de crecimiento:</strong> Son una representación visual de datos que muestran la probabilidad de determinadas medidas corporales de tus hijos (estatura, masa y talla). Estas contienen una serie de curvas de percentiles que
                comparan las medidas de tu hijo con otros niños de su misma edad y sexo.</li>
            <li><strong>Curvas (o percentiles) de crecimiento:</strong> Es una medida de posición estadística que compara la medida de tu hijo con otros 100 niños de su misma edad y sexo. Estas usualmente se muestran en las gráficas como líneas de clores.
                </li>
        </ol>
        <h2 id="2">Tablas de peso y talla de niños</h2>
        <p>Valorar el estos datos según las <strong>tablas</strong> estándar es fundamental. Estos datos nos brindan información para conocer si están teniendo una adecuada <strong>estatura</strong>, <a href="/etapas-de-la-ninez">desarrollo físico</a> y
            <a href="/generalidades-del-crecimiento-infantil/nutricion-malnutricion-y-crecimiento">nutricional</a>. Y poder identificar de manera temprana si hay <a href="/factores-que-perjudican-el-crecimiento">alteraciones</a> para facilitar la posibilidad
            de un tratamiento oportuno.</p>
        <p>Conocer cómo es el patrón normal de crecimiento de tu hijo usando las tablas de peso y talla permite detectar, de forma precoz, la aparición de determinados problemas, pero también contribuye a evitar intervenciones o estudios innecesarios en
