        <h3 id="3.2">¿Cómo se usan?</h3>
        <p>Puedes revisar si tu hijo está creciendo de manera correcta utilizando una <strong>gráfica de crecimiento</strong>. Detectar la curva de percentil tú mismo es fácil. Sin embargo, únicamente un médico pediatra endocrinólogo especialista es quien debe de diagnosticar y evaluar estas gráficas, ya que  estas no están diseñadas para usarse como un único instrumento de diagnóstico.Puedes revisar si tu hijo está creciendo de manera correcta utilizando una gráfica de crecimiento. Detectar la curva de percentil tú mismo es fácil. Sin embargo, únicamente un médico pediatra endocrinólogo especialista es quien debe de diagnosticar y evaluar estas gráficas, ya que  estas no están diseñadas para usarse como un único instrumento de diagnóstico.</p>
        <p>En las gráficas de crecimiento infantil existen importantemente dos variables: <strong>peso y estatura</strong>. El eje horizontal expresa la edad y el vertical, el peso y la talla. </p>
        <p>Además, contienen varias <strong>curvas</strong> que señalan los diferentes percentiles: 3, 10, 25, 50, 75, 90 y 97 (100%). Cada vez que el niño acuda al endocrinólogo pediatra este escribirá sus valores, en qué punto de la curva se encuentra y si hay algún problema.</p>
