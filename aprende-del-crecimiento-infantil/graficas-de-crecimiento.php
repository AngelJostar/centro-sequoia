        <p>Aunque una niña puede estar creciendo, su patrón puede desviarse de lo normal. Si sospecha que su hijo no está creciendo correctamente, consulte un <a href="/especialistas-en-crecimiento">médico especializado</a> de <a href="https://centrosequoia.com.mx/">Centro Sequoia</a>.</p>
        <p>Las <strong>tablas de crecimiento</strong> no deben utilizarse como instrumento único para tomar decisiones, aunque los datos que proporcionan contribuyen a formar una impresión global del niño. La estatura y masa corporal deben valorarse en un
            contexto amplio y requiere considerar otros <a href="/factores-que-perjudican-el-crecimiento">factores</a>, como la <a href="/factores-que-perjudican-el-crecimiento/baja-estatura-familiar">talla de la familia</a> o el ambiente.</p>
        <h3 id="2.3">Valores ideales</h3>
        <p>Será el médico pediatra endocrinólogo quien valore usando las tablas, si tu hijo está en sus valores correctos, sin embargo, tu hijo podría estar por debajo del promedio y no por ello dejaría de entrar dentro de la normalidad. También hay que
            valorar que un mismo niño puede pasar por <a href="/aprende-del-crecimiento-infantil/estiron-puberal">períodos más rápidos de aumento de estatura</a> y otros de desaceleración, sin que ello suponga nada anormal. </p>
        <p>No hay ningún valor ideal. Hay niños y niñas sanas de todos los pesos y tamaños, y un niño ubicado abajo del promedio puede estar tan sano como otro ubicado arriba del promedio.</p>
        <p>Idealmente, cada niño seguirá el mismo patrón a lo largo de su desarrollo, aumentando de peso y estatura al mismo ritmo, con una estatura proporcionada. </p>
        <h2 id="3">Graficas de crecimiento</h2>
        <p>Las <strong>gráficas de crecimiento</strong> son un conjunto de curvas (en una gráfica) que muestran cómo debe de estar creciendo en medidas corporales un niño. Estas comparan probabilísticamente el <strong>peso y talla</strong> de un niño con
            la de otros niños de su misma edad, sexo y raza. Los médicos pediatras endocrinólogos, enfermeras y padres deben utilizar las <strong>gráficas de crecimiento</strong> pediátrico para darle seguimiento al incremento de estatura masa de bebés,
            <a href="/etapas-de-la-ninez">niños</a> y <a href="/cambios-en-la-adolescencia">adolescentes.</a></p>
        <!-- IMAGEN -->
        <p>Las <strong>gráficas de crecimiento</strong> se elaboran con las mediciones de las <strong>tablas de peso</strong> y <strong>talla estatura</strong>, edad, y otros factores relativos al desarrollo de <strong>tus hijos</strong>. Estos datos se han tomado a lo largo y ancho de todo el planeta con grupos de niños de distintas edades y de distintos grupos raciales. Estos datos se someten a métodos estadísticos para diseñar los las <strong>curvas de crecimiento.</strong></p>
        <p>Existen <strong>gráficas</strong> para diferentes mediciones en hombres y mujeres.</p>
        <p>Hasta los dos años se miden el <strong>peso</strong>, la longitud (la medida de pies a cabeza con el niño acostado) y el perímetro cefálico (tamaño de la cabeza en redondo). A partir de los dos años y hasta el final del desarrollo se miden el <strong>peso y la talla</strong> (la medida de pies a cabeza estando de pie). También es útil conocer el índice de masa corporal (IMC).</p>
