        <p>Nota importante: Las <strong>gráficas de crecimiento</strong> no están diseñadas para usarse como un único instrumento de <strong>diagnóstico</strong>. En cambio, estas gráficas son herramientas que contribuyen a formar una idea general para el niño que se mide. Esto es un aspecto que tienes que tener en cuenta cuando vayas a <a href="/generalidades-del-crecimiento-infantil/como-medir-a-tu-hijo-de-manera-correcta">medir a tu hijo</a>. Estas tablas NO sustituyen a los consejos del pediatra, será tu endocrinólogo pediatra quién te dirá si el desarrollo de tu hijo está bien o mal.</p>
        <h3 id="3.1">¿Para qué sirven?</h3>
        <p>Las <strong>gráficas de crecimiento</strong> se usan para comparar la altura y el peso de tus hijos con los de niños de su misma edad.</p>
        <p>Pueden ayudarte a ti y a tu pediatra a revisar el desarrollo de tus hijos. Estas graficas te proporcionan una advertencia temprana de que tu hijo tiene un problema al crecer.</p>
