        <h2 id="4">Curvas de crecimiento en niños (Percentiles)</h2>
        <p>Las <strong>curvas de crecimiento</strong> en lsa gráficas indican cuántos otros niños en <strong>México</strong> pesan y miden cierta cantidad a cierta edad. Por ejemplo, la estatura en la línea del percentil 50 significa que la mitad de los chavos en México miden más que ese número y la otra mitad de los chavos miden menos.</p>
        <p>Se le llama <strong>percentiles</strong> a la métrica estadística que nos dice cómo es un valor en comparación a otros. En este caso, cómo es el peso, o la altura de un niño frente a otros de su edad y sexo. Cuanto mayor sea el número más alto será la niña respecto a los de su misma edad y, cuanto menor número, menos alta será comparada con niñas de su misma edad. </p>
        <p>Los <strong>percentiles</strong> son un porcentaje cuya interpretación necesita de alguien con conocimiento y experiencia en el crecimiento de los niños, sus variaciones normales y las que sugieren problemas. Por sí solos no indican el estado de salud de un niño. Es sólo una comparación. Este dato aislado concreto de un niño, no tiene demasiado valor. </p>
        <p>Además también es útil conocer la <strong>velocidad de aumento del peso y de la talla</strong>. Las tablas tienen más utilidad para realizar un seguimiento a lo largo del tiempo. Conocer los cambios en el desarrollo físico de los niños a diferentes <a href="/etapas-del-desarrollo-humano">edades</a> y conocer la curva específica de un niño puntual es más importante que el valor aislado. Si  en un momento dado cambia el patrón de crecimiento que seguía ese niño, y empieza a crecer más despacio, podemos estar ante un <a href="/factores-que-perjudican-el-crecimiento">problema de salud</a>  y descubrirlo observando su curva de crecimiento. </p>
        <h3 id="4.1">¿Cómo entender los percentiles?</h3>
        <p>El número de <strong>percentil</strong> de tu hijo o hija te indica si está por arriba o por debajo del medio respecto a otros chavos de su edad y sexo. Si tu hijo está en el <strong>percentil</strong> 50 estaría en la media exactamente. Es decir, si ponemos a 100 niños en fila por orden de estatura, él estaría exactamente en la mitad.</p>
        <p>Si tu hijo está en el <strong>percentil</strong> 3, 19 ó 25 estarían por debajo de la media (es más chaparro que la media) y si está en el <strong>percentil</strong> 75, 90 ó 97 se encuentra por encima (es más alto que la meda). Eso no quiere decir que unos niños sean más sanos que otros. Tanto si son más o menos altos y pesados  según la tabla es perfectamente normal.</p>
        <p>Algunos de los percentiles más comunes significan lo siguiente:</p>        
        <ul>
            <li> 50: La media exactamente.</li>
            <li>25: Sus valores está por debajo de la media. </li>
            <li>3: Está muy por debajo de la media.</li>
            <li>75: Sus valores son ligeramente superiores al de la media.</li>
            <li>97: Está en el límite de los parámetros normales. </li>
        </ul>
        <h3 id="4.2">Ejemplos</h3>
        <p>Aquí te dejamos un <strong>ejemplo</strong> para que entiendas mejor el uso de los percentiles:</p>
        <ul>
            <li>Si tu hija está en el percentil 75 de estatura, ella es más alta que el 75% de las otras niñas de su edad</li>
            <li>Si tu hija está en el percentil 25 de estatura, ella es más alta únicamente que el 25% de las otras niñas de su edad</li>
        </ul>
        <p>Para que lo entiendas más a fondo, te dejamos a continuación más ejemplos:</p>
        <p>Ejemplo 1: Si tu hijo pesa un valor que está en el percentil 10 significa que el 90% de los chavos son más pesados y tu hijo es flaco en comparación con otros chavos de su misma edad. </p>
        <p>Ejemplo 2: Si tu hija pesa un valor que esté en percentil 90, quiere decir que tu hija está bastante más pesada que otras niñas de su edad. Y que solo el 10% de las niñas pesan más. </p>
        <p>Ejemplo 3: Si tu hija está en el percentil 90 de estatura, quiere decir que es de las 10 niñas de cada 100 más altas. Ósea que tu hija a esa edad es más alta que casi todas los otras niñas.</p>
        <p>Ejemplo 4:, si tu hijo está en el percentil 10 de altura, significa que está muy por debajo de la altura media comparado con otros de su edad. Ósea que es bastante más chaparro.</p>
        <h2 id="5">Conclusión</h2>
        <p>Las <strong>tablas de crecimiento</strong> tienen mucha importancia y son bastante utilizados por los pediatras endocrinólogos. Con estas, pueden comparar que el desarrollo físico de tus hijos sea el adecuado. Si el niño no está creciendo de manera correcta, su médico deberá indagar las causas para poder encontrar una solución.  </p>
        <p>Hay que tener en mente que todas las <a href="/generalidades-del-crecimiento-infantil/como-medir-a-tu-hijo-de-manera-correcta">mediciones</a> de estatura y masa se deben hacer siempre con el pediatra, ya que el medirá la estatura entre otros aspectos (como la edad ósea) para evaluar el desarrollo de los niños y las niñas. </p>
