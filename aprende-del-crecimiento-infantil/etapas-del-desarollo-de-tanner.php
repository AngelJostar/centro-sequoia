        <h4>Estadio 1</h4>
        <p>Vamos a ver que los pechos de la niña son totalmente planos no va a ver ningún reborde y que el vello púbico prácticamente no va a estar presente.</p>
        <h4>Estadio 2</h4>
        <p>Vamos a ver que la niña empieza con un promontorio que lo llamamos <strong>botón mamario</strong>. Se llama botón mamario porque cambia la densidad del tejido cuándo nosotros lo palpamos se vuelve un poquito más duro en el centro, es como un pequeño
            montículo que toma en cuenta la aréola y el pezón de las glándulas mamarias, se infla un poquito hacia arriba. La mujer ya no es plana. </p>
        <p>También aparece un vello púbico muy lacio generalmente con poco color exactamente en medio de la vagina en los labios mayores, es decir una línea muy definida de vello. </p>
        <h4>Estadio 3</h4>
        <p>Ya hay aumento y elevación de <strong>pecho y aréola</strong>. Se forma un montículo ya no solo con el pezón y la aréola sino la glándula en si sobresale un poquito más y generalmente se inclina un poquito hacia arriba. El vello púbico aparece
            un poquito más esparcido ya no solo ocupa la parte media sino se empieza a esparcir hacia los lados. Esta etapa es importante porque es muy probable que la mujer ya inicie la <strong>menstruación</strong>.</p>
        <h4>Estadio 4</h4>
        <p>Podemos ver que las <strong>glándulas mamarias</strong> ya están mucho más formadas ya no solo forman un montículo el pezón y la aréola ya lo forma muy definido este montículo pero también la mama se eleva y tiende a caer un poquito más, ya no
            está tan elevada. El vello púbico se empieza a esparcir en forma triangular y no ocupa todavía los muslos como vamos a ver en la fase final.</p>
        <h4>Estadio 5</h4>
        <p>La mama está totalmente desarrollada. La <strong>mama</strong> tiende a caer por el peso del tejido graso. El vello púbico ya ocupa los muslos con un triángulo bien definido. Es generalmente cuando ya se han desarrollado perfectamente bien las
            características sexuales de la mujer y generalmente esto es de los 15 años en adelante.</p>
