        <h4>Estadio 1</h4>
        <p>Es una etapa <strong>prepuberal</strong> cuando el testículo se ubica en el tamaño 3, es menor de 2.5 cm en su longitud, esto generalmente es cuando el niño tiene menos de 11 años.</p>
        <h4>Estadio 2</h4>
        <p>Es cuando ya se inicia la <strong>pubertad</strong> esto ocurre la cuenca número 4 y es cuando los niños hombres ya empiezan a presentar un poquito de <strong>vello púbico</strong> en la base del pene. Esto generalmente se presenta entre 11 y 12 años y marca el inicio de la pubertad, se puede presentar incluso en forma tardía a los 13 años y nos puede hablar de que el niño no está produciendo en forma adecuada testosterona o bien puede ser un factor heredado del padre cuando el padre también fue un <strong>madurador tardío</strong>.</p>
        <h4>Estadio 3</h4>
        <p>Hay crecimiento del pene y mayor crecimiento del testículo. Esta escala implica una cuenca número diez en el <strong>orquidómetro</strong> y nos habla de que el niño empieza su desarrollo de lleno. Es cuando el niño empieza a crecer fuertemente y se produce un disparo del crecimiento. </p>
        <h4>Estadio 4</h4>
        <p>Se presenta cuando el testículo ya está midiendo más o menos entre 15 y 20 en el número 16 del <strong>orquidometro</strong>. Es entre los 14 años y medio y 16 años y está marcando el final tanto de la pubertad como del crecimiento. </p>
        <h4>Estadio 5</h4>
        <p>Ya son genitales de <strong>adulto</strong> es una cuenca número 25 en el <strong>orquidometro</strong> y reafirma que el niño ya alcanzó su adultez en cuento a su <strong>desarrollo sexual</strong> o sus caracteres secundarios sexuales. </p>
