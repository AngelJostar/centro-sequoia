            <h2>1.- Desarrollo prenatal:</h2> 
            <p>Ocurre la concepción y da comienza el desarrollo. Hay  tres etapas de desarrollo prenatal: períodos germinal, embrionario y fetal, donde comienza la formación de todas las estructuras principales del cuerpo.</p>
            <h2>2.- Infancia y niñez:</h2> 
            <p>El primer año y medio a dos años de vida son de crecimiento y cambio dramáticos. El desarrollo del cerebro ocurre a un ritmo notable, al igual que el crecimiento físico y el desarrollo del lenguaje. </p>
            <h2>3.- Niñez temprana:</h2> 
            <p>Tambien conocido como años preescolares. En esta etapa alrededor de los 2 a 6 años, ocurren cambios increibles de crecimiento y cambios en el vocabulario. Los niños de preescolar comienzan a tener concepciones interesantes sobre el tamaño, el tiempo y el espacio y la distancia, ademas de desarrollar sentimientos como la culpa cuando sienten desaprobacion de los demás. </p>
            <h2>4.- Niñez media:</h2> 
            <p>la mayor parte de lo que tradicionalmente es la escuela primaria, o las edades comprendidas entre los 6 y los 11 años. El mundo se convierte en un mundo de aprendizaje y prueba de nuevas habilidades y de evaluación de las propias habilidades y logros a través de deportes de equipo, puntajes de exámenes y otras formas de reconocimiento. El cerebro alcanza su tamaño adulto alrededor de los siete años, pero continúa desarrollándose.  Las tasas de crecimiento se ralentizan y los niños pueden perfeccionar sus habilidades motoras en este momento de la vida. Las amistades entre personas del mismo sexo son particularmente destacadas durante este período. </p>
            <h2>5.- Adolescencia:</h2> 
            <p>La adolescencia, o la edad aproximadamente entre los 12-18 años, está marcada por la pubertad y la maduración sexual, acompañadas de importantes cambios socioemocionales. El tiempo puede variar según el género, la cohorte y la cultura. También es un momento de cambio cognitivo cuando el adolescente comienza a pensar en nuevas posibilidades y a considerar conceptos abstractos como el amor, el miedo y la libertad. Una tarea importante del desarrollo durante la adolescencia consiste en establecer la propia identidad, se esfuerzan por tener un sentido de pertenencia y aceptación. Se exploran nuevos roles y responsabilidades, que pueden involucrar citas, conducir, aceptar un trabajo de medio tiempo y planificar futuros académicos.  </p>
            <h2>6.- Adultez temprana:</h2>
            <p>La edad adulta temprana, aproximadamente entre los 20 y los 40 años, puede dividirse en otra categoría más de “edad adulta emergente”. Es un momento para enfocarse en el futuro y poner mucha energía en tomar decisiones que ayudarán a uno a ganarse el estatus de un adulto completo a los ojos de los demás. El amor y el trabajo forman parte de las principales preocupaciones en esta etapa de la vida.</p>
            <h2>7.- Edad adulta media:</h2> 
            <p>La edad adulta media abarca los años entre los 40 y los 65 años. Este es un período en el que el envejecimiento fisiológico que comenzó antes se vuelve más notable y un período en el que muchas personas están en su punto máximo de productividad en el amor y el trabajo. También puede ser un momento para volverse más realista sobre las posibilidades en la vida; de reconocer la diferencia entre lo que es posible y lo que es probable</p>

