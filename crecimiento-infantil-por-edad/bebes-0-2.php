</div>
<p><span style="font-weight: 400;">Los </span><b>pediatras endocrinólogos</b><span style="font-weight: 400;"> señalan la importancia de entender comprender la diferencia entre altura y crecimiento.</span></p>
<ol>
<li><b>a)</b> <span style="font-weight: 400;">Primero, la altura se refiere simplemente a qué tan alto o bajo podría ser tu hijo en un momento dado.</span></li>
<li><b>b)</b> <span style="font-weight: 400;">El crecimiento de tu pequeño es la medida dinámica de cómo cambia la altura con el tiempo y se utiliza para determinar cualquier anomalía nutricional de salud, o trastornos de crecimiento subyacentes.</span></li>
</ol>
<p><span style="font-weight: 400;">El </span><b>crecimiento normal</b><span style="font-weight: 400;"> es una concordancia de su estatura por edad, cuando caen dentro del rango promedio de lo que se espera. Existe una variación natural en la estatura de todos los niños, incluso, y los de la misma familia pueden ser más bajos o más altos que los demás.</span></p>
