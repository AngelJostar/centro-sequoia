                <h2 id="1">Qué es la adolescencia</h2>
                <p><b>La adolescencia</b> es el período en la vida de una persona que sucede entre el final de <b> la niñez </b>y el inicio de la edad adulta, cuando se ha completado el desarrollo del organismo. Este es el momento en el que maduran las <a href="/generalidades-del-crecimiento-infantil/etapas-del-desarollo-de-tanner/"> características físicas y sexuales </a>de un niño y ocurre debido a <b> cambios hormonales.</b> La adolescencia también se conoce como: </p>
                <ul>
                    <li>Pubertad</li>
                    <li> Maduración sexual
                    <li>Desarrollo reproductivo</li>
                    <li>Maduración sexual</li>
                    <li>Menarquia</li>
                    <li>Adrenarquia</li>
                </ul>
                <p>Es una época de <b>maduración</b> psicológica, en la que una persona se vuelve <b>"adulta"</b> en su comportamiento y en su apariencia física.</p>
                <p>Durante la <b>pubertad</b>, las glándulas endocrinas producen hormonas que provocan cambios corporales y el desarrollo de características sexuales secundarias.</p>
                <ul>
                    <li><b>En el caso de los niños:</b> los testículos generan un aumento en la producción de testosterona .</li>
                    <li><b>En el caso de las niñas:</b> los ovarios aumentan la producción de estrógeno y además de otras hormonas femeninas.</li>
                </ul>
                <div class="contenedor-video mt-1">
                        <div class="reproductor" data-id="6mSMpErXlFQ"></div>
                </div>
                <h2 id="2">¿A qué edad comienza? </h2>
                <p><a href="/crecimiento-infantil-por-edad/adolecentes-12-16/">La edad</a>   en la que el <b>niño</b> entra en la <b>pubertad</b> depende de diferentes aspectos, como los <a href="/generalidades-del-crecimiento-infantil/factores-que-afectan-el-crecimiento"> genes</a>, el género y la nutrición. Esta comienza cuando los cambios en el cerebro de su hijo hacen que las hormonas sexuales de las gónadas (que son los ovarios y los testículos) comiencen a liberarse.</p>
                <p>Esto suele ocurrir alrededor de los 10-11 años para las <b>niñas</b> y alrededor de los 11-12 años para los niños. Pero es normal que el inicio de la pubertad oscile entre los 8 y 13 años en las niñas y entre los 9 y 14 años en los niños.</p>
                <p>No hay forma de saber exactamente cuándo comenzará la pubertad  <b>su hijo</b>. Los primeros cambios en el cerebro y los niveles hormonales de su hijo no se pueden ver desde el exterior, por lo que es fácil pensar que la pubertad no ha comenzado.</p>
                <p><b>La adolescencia</b> se puede completar en aproximadamente 18 meses o puede demorar hasta 5 años. Este rango también es completamente normal.</p>
                <p><u>Puntos clave:</u></p>
                <ul>
                    <li>La adolescencia generalmente comienza a los 10-11 años para las niñas y a los 11-12 años para los niños. Puede ser antes o después.</li>
                    <li> En este periodo los niños se vuelven más altos, más pesados y más fuertes.</li>
                    <li>También hay cambios en los órganos sexuales, el cerebro, la piel, el cabello, los dientes y la sudoración de los niños.</li>
                </ul>
                <h2 id="3">Los cambios físicos en la adolescencia </h2>
                <p>La adolescencia se compone de una <b>secuencia de etapas</b> que afectan el esqueleto, el músculo, el sistema reproductor y casi todos los demás sistemas corporales. Los cambios físicos durante la pubertad tienden a ser graduales y constantes. </p>
                <p>En este periodo el adolescente experimenta no solo un crecimiento y cambio físico, sino también un cambio y crecimiento emocional, psicológico, social y mental.</p>
                <h3 id="3.1">Cambios previos a la adolescencia</h3>
                <p>Varios cambios ocurren durante la pre-pubertad:</p>
                <ul>
                    <li>Los niños se vuelven más fuertes a medida que aumenta su masa muscular.</li>
                    <li>Las habilidades motoras, tanto de fuerza como de coordinación, mejoran.</li>
                    <li>El cabello de un niño en edad escolar puede oscurecerse un poco.</li>
                </ul>
                <p>La textura y la apariencia de la piel de un niño cambian gradualmente, volviéndose más parecida a la de un adulto.</p>
                <h3 id="3.2">Cambios físicos en niños </h3>
                <p>En los niños, estos son los principales cambios físicos externos en la pubertad que puede esperar.</p>
                <div class="tamaño-img">
                    <img width="676" height="1200"  src="/img/crecimiento-infantil-por-edad/signos-pubertad-ninos-1.webp" alt="Infografía de signos de la pubertad en los niños"></a>
                </div>
                <h4 id="3.2.1">Alrededor de los 11-12 años </h4>
                <ul>
                    <li>Los genitales externos (pene, testículos y escroto) comenzarán a crecer. Es normal que un testículo crezca más rápido que el otro. Puede asegurarle a su hijo que los testículos de los hombres generalmente no son del mismo tamaño.</li>
                    <li>El vello púbico comenzará a crecer. Se volverá más oscuro y espeso con el tiempo.</li>
                </ul>
                <h4 id="3.2.2">Alrededor de los 12-14 años </h4>
                <ul>
                    <li> Su hijo tendrá un crecimiento acelerado. Su hijo crecerá más alto y su pecho y hombros se ensancharán. Algunas partes del cuerpo de su hijo, como la cabeza, la cara y las manos, pueden crecer más rápido que las extremidades y el torso. Esto puede hacer que su hijo se vea desproporcionado durante un tiempo. En promedio, los niños crecen entre 10 y 30 cm. Suelen dejar de crecer alrededor de los 18-20 años.</li>
                    <li>Es común que los varones tengan un desarrollo mamario menor. Si a su hijo le preocupa esto, podría ayudarlo a saber que es normal y que, por lo general, desaparece por sí solo. Si no desaparece o si los senos parecen estar creciendo mucho, consulte a su médico de cabecera.</li>
                    <li>El cabello comenzará a crecer en otras partes del cuerpo de su hijo: debajo de los brazos, en la cara y en el resto del cuerpo. El vello de las piernas y los brazos se espesará. A algunos hombres jóvenes les crecerá más vello corporal hasta los 20 años.</li>
                    <li>Se produce la hormona testosterona, que estimula a los testículos a producir espermatozoides.</li>
                    <li>Su hijo puede comenzar a tener erecciones y eyacular (liberar esperma). Durante este período, las erecciones a menudo ocurren sin ningún motivo. Simplemente dígale a su hijo que esto es normal y que las personas generalmente no se dan cuenta. La eyaculación durante el sueño a menudo se denomina "sueño húmedo".</li>
                </ul>
                <h4 id="3.2.3">Alrededor de los 14-15 años </h4>
                <p>La laringe ("manzana de Adán" o laringe) se volverá más obvia. La laringe de su hijo se agrandará y su voz se "romperá" y eventualmente se hará más profunda. Las voces de algunos niños se mueven de alto a bajo y viceversa, incluso en una oración. Esto se detendrá a tiempo.</p>
                <div class="tamaño-img">
                    <img width="676" height="1200"  src="/img/crecimiento-infantil-por-edad/signos-pubertad-ninos-2.webp" alt="Infografía de signos de la pubertad en los niños"></a>
                </div>
                <p>Las glándulas suprarrenales producen hormonas que aumentan la sudoración en las axilas, el olor corporal, el acné, el vello púbico y axilar. Este proceso se llama adrenarquia. El niño con frecuencia hará uso de desodorante para las axilas durante este tiempo.</p>
                <h3 id="3.3">Cambios físicos en niñas</h3>
                <p>En las <b>niñas</b>, estos son los principales <b>cambios físicos</b> externos en la pubertad que puede esperar.</p>
                <div class="tamaño-img">
                    <img width="676" height="1200"  src="/img/crecimiento-infantil-por-edad/signos-pubertad-ninas-1.webp" alt="Infografía de signos de la pubertad en las niñas"></a>
                </div>
                <h4 id="3.3.1">Alrededor de los 10-11 años</h4>
                <ul>
                    <li> Los senos comenzarán a desarrollarse. Este es el primer signo visible de que comienza la pubertad. Es normal que los senos izquierdo y derecho crezcan a diferentes velocidades. También es común que los senos estén un poco sensibles a medida que se desarrollan. Si su hija quiere un sostén, un top corto suave o un sostén deportivo puede ser una buena primera opción.</li>
                    <li>Se produce un brote de crecimiento. Algunas partes del cuerpo, como la cabeza, la cara y las manos, pueden crecer más rápido que las extremidades y el torso. Esto puede hacer que su hijo se vea desproporcionado durante un tiempo. En promedio, las niñas crecen de 5 a 20 cm. Suelen dejar de crecer alrededor de los 16-17 años.</li>
                    <li>La forma del cuerpo cambiará. Por ejemplo, las caderas de una niña se ensancharán.</li>
                    <li>Los genitales externos (vulva) y el vello púbico comenzarán a crecer. El vello púbico se oscurecerá y se volverá más grueso con el tiempo.</li>
                </ul>
                <h4 id="3.3.2">Alrededor de los 12 a 14 años</h4>
                <ul>
                    <li> El cabello comenzará a crecer debajo de los brazos.</li>
                    <li>Una secreción transparente o blanca de la vagina comienza varios meses antes de que comiencen los períodos. Si la secreción le molesta a su hijo, podría sugerirle que use un protector de bragas. Si su hijo dice que le pica, le duele o huele mal, consulte a su médico de cabecera.</li>
                    <li>Por lo general, los períodos comenzarán dentro de los 2 años posteriores al inicio del crecimiento de los senos, pero pueden demorar hasta 4 años.</li>
                </ul>
                <div class="tamaño-img">
                    <img width="676" height="1200"  src="/img/crecimiento-infantil-por-edad/signos-pubertad-ninas-2.webp" alt="Infografía de signos de la pubertad en las niñas"></a>
                </div>
                <h2 id="4">Cambios cognitivos</h2>
                <p>La <b>adolescencia</b> trae <b>muchos cambios</b>. Estos no son solo cambios físicos, sino también <b>mentales</b>. Durante estos años, los adolescentes se vuelven más capaces de pensar de manera abstracta. Con el tiempo, pueden hacer planes y establecer metas a largo plazo. Cada niño puede progresar a un ritmo diferente y puede tener una visión diferente del mundo. En general, estas son algunas de las habilidades que puede ver en su adolescente:</p>
                <ul>
                    <li>Desarrolla la capacidad de pensar de manera abstracta.</li>
                    <li>Se preocupa por la filosofía, la política y los problemas sociales.</li>
                    <li>Piensa a largo plazo</li>
                    <li>Establece metas</li>
                    <li>Se compara a sí mismo con sus compañeros.</li>
                </ul>
                <p>A medida que su adolescente comienza a luchar por la independencia y el control, pueden ocurrir muchos cambios. Estos son algunos de los problemas que pueden afectar a su hijo adolescente durante estos años:</p>
                <ul>
                    <li> Quiere independencia de los padres</li>
                    <li>La influencia y aceptación de los compañeros se vuelve muy importante</li>
                    <li>Las relaciones románticas y sexuales se vuelven importantes</li>
                    <li>Puede estar enamorado</li>
                    <li>Tiene un compromiso a largo plazo en la relación.</li>
                </ul>
                <h2 id="5">Adolescencia cambios psicológicos </h2>
                <b>Los <b>cambios emocionales</b> a veces ocurren antes de los cambios físicos de la pubertad. Muchos jóvenes quieren tener un mayor control de sus vidas a medida que atraviesan la pubertad.</b>
                <p>Puede encontrar que es:</p>
                <ul>
                    <li> experimentando un mayor sentido de sí mismo.</li>
                    <li>comenzando a tener un interés sexual en otras personas.</li>
                    <li>experimentando cambios de humor, energía y patrones de sueño.</li>
                </ul>
                <p>Los desafíos emocionales que pueden surgir en este momento incluyen:</p>
                <ul>
                    <li> hacer frente a un cuerpo cambiante y sentirse cohibido por la apariencia.</li>
                    <li>sentirse frustrado cuando no puede lograr lo que desea lograr y enojarse.</li>
                </ul>
                <p>Estos cambios emocionales son una parte importante en la elaboración de su propia identidad y valores morales.</p>
                <h2 id="6">Crecimiento en adolescentes </h2>
                <p>La <b>adolescencia</b> es un momento de <b>crecimiento acelerado</b>. Un adolescente puede crecer muchos centímetros en pocos meses, seguido de un período de crecimiento muy lento. Y después tener otro <a href="/generalidades-del-crecimiento-infantil/estiron-puberal/"> brote de crecimiento rápido.</a></p>
                <!-- imagen -->
                <p>El crecimiento normal, respaldado por una buena <a href="/generalidades-del-crecimiento-infantil/nutricion-malnutricion-y-crecimiento"> nutrición</a>, sueño suficiente y ejercicio regular, es uno de los mejores indicadores generales de la buena salud de su hijo adolescente.</p>
                <p>A pesar de los datos recopilados para las <a href="/generalidades-del-crecimiento-infantil/graficas-de-crecimiento/">tablas de crecimiento</a> , las <b>alturas y pesos</b> "normales" son difíciles de definir. El patrón de crecimiento de su hijo adolescente está determinado en gran medida por la genética. Los padres más bajos, por ejemplo, tienden a tener hijos más bajos, mientras que los padres más altos tienden a tener hijos más altos.</p>
                <p>Aunque puede preocuparse <b>si su hijo no es tan alto como otros compañeros de clase</b>, la pregunta más importante es <a href="/crecimiento-infantil/"> si su hijo continúa creciendo</a>  a un ritmo normal. Si su médico detecta un problema, como una <a href="/generalidades-del-crecimiento-infantil/graficas-de-crecimiento/">tasa de crecimiento</a> que había sido normal pero que se ha estabilizado recientemente, puede realizar un seguimiento de las medidas de su hijo cuidadosamente durante varios meses para ver si el patrón de <b>crecimiento</b> sugiere un posible problema de salud o es solo una variación de normal.</p>
                <p>No es inusual que los adolescentes tengan sus propias preocupaciones sobre cómo están creciendo y cómo se ven. Las niñas pueden ser muy críticas con su propio peso, lo que a veces puede generar <a href="/importancia-de-ser-mas-alto/autoestima/"> preocupaciones sobre la imagen corporal</a> y <a href="/generalidades-del-crecimiento-infantil/nutricion-malnutricion-y-crecimiento"> prácticas dietéticas</a> poco saludables. Los niños tienden a estar más preocupados por su <b>altura</b> y <a href="/crecimiento-infantil-deficiente/ejercicios-para-crecer-de-estatura/"> desarrollo muscular</a>, lo que también puede llevar a prácticas poco saludables, como el uso de esteroides y suplementos proteicos.</p>
                <p>Si le preocupa la imagen corporal de su hijo adolescente o sus hábitos alimenticios y de ejercicio, hable con el médico.</p>
                <p>Muchos adolescentes se preocupan mucho por ser diferentes de sus compañeros y por cualquier cosa que los haga no encajar o parecer "normales". Anime a su hijo adolescente a plantear cualquiera de estas inquietudes con el <a href="Centrosequoia.com.mx"> médico</a>, si se siente cómodo haciéndolo. El médico puede asegurarle que otros niños tienen las mismas preocupaciones sobre su tamaño.</p>
                <p>Si tiene otras inquietudes sobre el crecimiento o desarrollo de su hijo adolescente, hable con su médico.</p>
