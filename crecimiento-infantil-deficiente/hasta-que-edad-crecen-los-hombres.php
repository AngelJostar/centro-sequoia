                <p>Un estudio observacional de 2015, Trust Source, analizó el vínculo entre los estimulantes centrales para tratar el TDAH y el retraso en el crecimiento. El estudio evaluó a 410 niños de entre 0,9 y 16,1 años. Los investigadores encontraron que la medicación provocó una interrupción temporal en los aumentos de peso y altura. Incluso después de tomar medicamentos durante seis años, los niños aún no habían alcanzado el crecimiento previsto. </p>
                <p>Otro estudio, una fuente confiable, publicado en 2014, siguió a una cohorte de 340 niños con TDAH hasta la edad adulta. Llegaron a la conclusión de que la medicación estimulante no afectó la altura de los niños en la edad adulta. </p>
