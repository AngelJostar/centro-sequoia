                            pueden ser más bajos cuando son adultos.</li>
                        <li>Condiciones genéticas: se espera que los niños con síndrome de Down, síndrome de Noonan y síndrome de Turner sean más bajos que sus compañeros. Las personas con síndrome de Marfan tienden a ser más altas.</li>
