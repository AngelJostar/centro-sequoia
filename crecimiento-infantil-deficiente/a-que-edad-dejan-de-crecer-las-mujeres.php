                        <li>Desequilibrios hormonales: los niveles bajos de la hormona del crecimiento o de la tiroides, por ejemplo, pueden provocar tasas de crecimiento más lentas y una estatura adulta más baja.</li>
                        <li>Medicamentos: el uso de algunos medicamentos puede retardar el crecimiento. El uso crónico de corticosteroides es un posible ejemplo de esto. Sin embargo, las enfermedades crónicas que a menudo requieren corticosteroides, como
