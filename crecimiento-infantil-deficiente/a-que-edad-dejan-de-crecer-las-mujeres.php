                <h2><b>Cuando ver a un doctor</b></h2>
            </div>
            <div class="justificar-texto-85 justificar-derecha-texto mt-2 ">
                <p>Si su hija parece estar muy por encima o por detrás de sus compañeros en el desarrollo físico, puede ser el momento de hablar con su pediatra. Las niñas que no muestran signos de desarrollo de los senos a los 13 años y / o que no tienen
                    su primera menstruación a los 15 o 16 años se consideran retrasadas en la pubertad. Por otro lado, una niña que muestra signos de pubertad a los 6 o 7 años está experimentando una <a href="/padecimentos-que-afectan-el-crecimiento/pubertad-precoz/"> pubertad precoz.</a></p>
                <p>Con cualquiera de esos extremos, pídale a su pediatra que le eche un vistazo porque podría haber problemas médicos o endocrinos (hormonales) que causen una pubertad precoz o tardía. Los pediatras pueden hacer una radiografía de la edad
                    ósea en el consultorio para determinar si su hija está en camino de alcanzar la estatura promedio de un adulto.</p>
            </div>
            <div class=" pt-15 justificar-texto-90  ">
                <h2><b>¿Qué hacer al respecto?</b></h2>
            </div>
            <div class="justificar-texto-85 justificar-derecha-texto mt-2 ">
                <p>Las mujeres pueden ganar dos centímetros o más de <b> altura </b> desde la niñez hasta la pubertad. Dormir lo suficiente, comer alimentos nutritivos y hacer ejercicio con regularidad son buenos hábitos que pueden ayudarlos a crecer de
                    manera saludable.</p>
                <p>Si le preocupa el patrón de crecimiento de su hija, <a href="/contact"> comuníquese con un médico de Centro Sequoia </a> lo antes posible.</p>
                <p>Es probable que su médico le pregunte sobre el historial de crecimiento de su familia. Examinarán a su hija y observarán cuidadosamente su <a href="/aprende-del-crecimiento-infantil/graficas-de-crecimiento/"> curva de crecimiento.</a></p>
                <p>A veces, su médico puede usar pruebas como <a href="/aprende-del-crecimiento-infantil/edad-osea"> radiografías </a> o análisis de sangre para ayudarlos a determinar las causas de los retrasos en el crecimiento.</p>
            </div>
            <div class=" pt-15 justificar-texto-90  ">
                <h2><b>En la consulta del doctor</b></h2>
            </div>
            <div class="justificar-texto-85 justificar-derecha-texto mt-2 ">
                <p>El <b> crecimiento normal,</b> respaldado por una buena nutrición, sueño suficiente y ejercicio regular, es uno de los mejores indicadores generales de la buena salud de su hijo adolescente.</p>
                <p>A pesar de los datos recopilados para las tablas de crecimiento, las alturas y pesos "normales" son difíciles de definir. El patrón de <b> crecimiento de su hija adolescente </b> está determinado en gran medida por la genética. Los padres
                    más bajos, por ejemplo, tienden a tener hijos más bajos, mientras que los padres más altos tienden a tener hijos más altos.</p>
                <p>Aunque puede preocuparse si su hija no es tan alto como otras compañeras de clase, la pregunta más importante es si su hija continúa creciendo a un ritmo normal. Si su médico detecta un problema, como una <b> tasa de crecimiento </b> que
                    había sido normal pero que se ha estabilizado recientemente, es posible que realice un seguimiento de las medidas de su hijo con cuidado durante varios meses para ver si el patrón de crecimiento sugiere un posible problema de salud
                    o es solo una variación de normal.</p>
                <p>No es inusual que <b> las adolescentes </b> tengan sus propias preocupaciones sobre cómo están creciendo y cómo se ven. Las niñas pueden ser muy críticas con su propio peso, lo que a veces puede generar preocupaciones sobre la imagen corporal
                    y prácticas dietéticas poco saludables. Los niños tienden a estar más preocupados por su altura y desarrollo muscular, lo que también puede llevar a prácticas poco saludables, como el uso de esteroides y suplementos proteicos.</p>
                <p>Si le preocupa la imagen corporal de su hijo adolescente o sus hábitos alimenticios y de ejercicio, hable con el médico.</p>
                <p><b>Si tiene otras inquietudes sobre el crecimiento o desarrollo de su hijo adolescente, hable con un médico de Centro Sequoia, especializado en crecimiento y desarrollo infantil.</b></p>
