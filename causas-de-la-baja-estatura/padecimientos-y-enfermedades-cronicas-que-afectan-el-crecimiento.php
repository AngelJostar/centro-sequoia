                <p>Mientras que el retraso en el crecimiento puede ser la característica patológica de presentación inicial, el patrón de crecimiento constituye también una medida útil de la gravedad de la enfermedad y de la respuesta al tratamiento. </p>
                <p>El deterioro del crecimiento en niños con enfermedades crónicas se vincula al trastorno del eje de la hormona del crecimiento (HC)–factor de crecimiento de tipo insulínico (FCI) y predominantemente resulta de la desnutrición, inflamación crónica y el tratamiento corticosteroideo prolongado. La desnutrición ocasiona adaptaciones importantes en el sistema endocrino encaminadas a conservar la energía, desviar sustratos apartándolos del crecimiento y la reproducción y proporcionar fuentes de energía alternativas para la homeostasis crítica del organismo. Si bien los procesos inflamatorios crónicos exacerban la desnutrición, las citocinas proinflamatorias, como la interleucina-6, el factor de necrosis tumoral y la interleucina-1 también afectan adversamente al crecimiento, independientemente de la nutrición, a través de la perturbación de la integridad del eje HC– FCI y por efectos locales directos sobre la condrogénesis de la placa de crecimiento. </p>
                <p>Estos mecanismos que regulan el crecimiento son perturbados, más adelante, por los corticosteroides utilizados en algunos procesos crónicos, debido a sus propiedades antiinflamatorias e inmunodepresoras. Es probable que los procesos que suprimen el crecimiento retrasen la senescencia de la placa de crecimiento, lo cual, a su vez, permite recuperar el crecimiento en el momento de la normalización de las enfermedades crónicas y la retirada del tratamiento corticosteroideo.</p>
                <p>El deterioro del crecimiento lineal puede además exagerarse por el retraso en la pubertad, que se asocia frecuentemente a enfermedades crónicas. Las desviaciones del patrón de crecimiento normal pueden ser las primeras pistas.</p>
                <p>En niños conocidos por presentar una enfermedad crónica, los registros seriados de la talla/longitud y el peso, representados en gráficas de distancia estándar, son vitales para monitorizar el bienestar, la actividad patológica, los efectos clínicos adversos y la respuesta al tratamiento. En niños con enfermedades resistentes, se destaca cada vez más el carácter problemático, tanto física como psicológicamente, del deterioro del crecimiento, la estatura baja, el retraso en la pubertad y la reducción de la talla adulta. En este trabajo presentamos una actualización de la fisiopatología general del deterioro del crecimiento lineal en enfermedades crónicas y algunos procesos clave en los que aparece un considerable deterioro del crecimiento.</p>
