                <h3 >Fibrosis quística:</h3>
                <p>Es una enfermedad multisistémica progresiva crónica que afecta a las glándulas exocrinas, fundamentalmente a las vías respiratorias, al páncreas y a las glándulas sudoríparas. Los pacientes presentan infecciones e inflamación respiratorias crónicas, insuficiencia pancreática, menoscabo de la calidad de vida y reducción de la expectativa de vida. La desnutrición es consecuencia de la malabsorción, la anorexia, la ingestión insuficiente y el incremento de las necesidades. La ganancia de peso y el crecimiento lineal deficientes aparecen desde el nacimiento hasta el momento del diagnóstico y la incapacidad de medrar es una de las características de presentación clásicas de la Fibrosis quística después del periodo neonatal.</p>
