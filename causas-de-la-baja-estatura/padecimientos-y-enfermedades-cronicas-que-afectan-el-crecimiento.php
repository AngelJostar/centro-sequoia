                <h3>Celiaquía</h3>
                <p>La enfermedad celíaca (EC) es una dolencia crónica autoinmune, desencadenada por el consumo de gluten, que aparece en personas con predisposición genética. El intestino sufre un proceso inflamatorio que en ocasiones puede llegar a la atrofia de las vellosidades. Esto hace que los nutrientes no logren una adecuada absorción, lo que produce diversos síntomas clínicos que dependerán de la edad del diagnostico y la severidad de la lesión intestinal.</p>
                <p>Los síntomas clásicos incluyen: diarrea crónica, vómitos, cambios de carácter, falta de apetito, estacionamiento de la curva de peso y retraso del crecimiento.</p>
            </div>
            <div class="justificar-texto-90 justificar-derecha-texto">
                <h3>Enfermedad de Crohn </h3>
                <p>La enfermedad de Crohn y la colitis ulcerosa son las dos formas más frecuentes de enfermedad intestinal inflamatoria crónica en los niños. La inflamación afecta corrientemente al intestino delgado y/o al intestino grueso, pero puede afectar a todo el sistema gastrointestinal en la enfermedad de Crohn y se limita al intestino grueso en la colitis ulcerosa. Aunque los síntomas clásicos son diarrea crónica, dolor abdominal y pérdida de peso, la diarrea es clásicamente sanguinolenta en la colitis ulcerosa. La reducción de las velocidades de la talla y el peso son características comunes de ambos procesos.</p>
            </div>
