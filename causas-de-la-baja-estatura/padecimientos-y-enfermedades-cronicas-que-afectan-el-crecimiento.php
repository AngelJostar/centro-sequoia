                <h3>Artritis juvenil idiopática </h3>
                <p>Los retrasos en la talla son mayores en pacientes con artritis sistémica y artritis poliarticular con factor reumatoide positivo que en oligoartritis y artritis con factor reumatoide negativo. El retraso en la pubertad deteriora adicionalmente el crecimiento lineal en estos pacientes y se observa un mayor deterioro del crecimiento durante los años puberales en comparación con la situación anterior a la pubertad.</p>
                <p>Cualquier patología crónica (gastrointestinal, renal, cardíaca, pulmonar, hematooncológica…), si es lo suficientemente grave y prolongada, puede provocar un hipocrecimiento. En general, cuanto más precoz, grave y prolongada sea la enfermedad, menos posibilidades hay de que pueda recuperarse completamente.</p>
                <p>Los factores etiopatogénicos implicados en el fracaso del crecimiento asociado a las enfermedades crónicas son múltiples y varían dependiendo de la enfermedad de base, pero suelen estar en relación con: malnutrición, alteraciones metabólicas, efectos secundarios de la terapia, infecciones sobreañadidas y, posiblemente también, con los trastornos psicológicos que acompañan al padecimiento de una enfermedad crónica. Dentro de ellos, la malnutrición, a la que se puede llegar por múltiples mecanismos (disminución de aportes o incremento de pérdidas o demandas), es uno de los factores más importantes y constantes. En lo que se refiere a las manifestaciones clínicas, las específicas de la enfermedad de base serán, en la mayoría de los casos, las predominantes en el cuadro clínico y el fracaso del crecimiento será solo un síntoma más.</p>
