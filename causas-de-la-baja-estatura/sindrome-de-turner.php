                <h2><b>Síndrome de Turner Causas </b></h2>
            </div>
            <div class="justificar-texto-85 justificar-derecha-texto pt-15 ">
                <p>Casi todos los seres humanos nacemos con dos cromosomas sexuales. Los niños heredan el cromosoma X de sus madres y el cromosoma Y de sus padres. Las niñas heredan un cromosoma X de cada padre. En el caso de las <b> niñas que padecen del síndrome de Turner</b> les falta (parcial o totalmente) una copia del cromosoma X.</p>
            </div>
            <div class="tamaño-img ">
                <img src="/img/causas-de-la-baja-estatura/causas-del-sindrome-de-turner.webp" alt="En el caso de las niñas que padecen del síndrome de Turner les falta una copia del cromosoma X">
            </div>
            <div class="justificar-texto-85 justificar-derecha-texto pt-15 ">
                <p>Las variaciones en los genes que producen Síndrome de Turner podrían ser:</p>
            </div>
            <div class="contenedor-85porciento ">
                <div class="justificar-texto-90-listas">
                    <ul>
                       <li><h4>Monosomía.</h4> La ausencia completa de un cromosoma X generalmente ocurre debido a un error en el esperma del padre o en el óvulo de la madre. </li>
                       <li><h4>Mosaicismo.</h4> Se produce un error en la división celular durante las primeras etapas del desarrollo fetal. Esto da como resultado que algunas células del cuerpo tengan dos copias completas del cromosoma X. Otras células tienen solo una copia del cromosoma X.</li>
                       <li><h4>Anomalías del cromosoma X.</h4> Pueden existir partes anormales o faltantes de uno de los cromosomas X. Entonces las células tienen una copia completa y una alterada. Este error puede ocurrir en el esperma o el óvulo y todas las células tienen una copia completa y otra alterada. O el error puede ocurrir en la división celular en el desarrollo fetal temprano, de modo que solo algunas células contienen las partes anormales o faltantes de uno de los cromosomas X (mosaicismo).</li>
                       <li><h4>Material del cromosoma Y.</h4> En un pequeño porcentaje de casos de síndrome de Turner, algunas células tienen una copia del cromosoma X y otras células tienen una copia del cromosoma X y algo de material del cromosoma Y. Estos individuos se desarrollan biológicamente como mujeres, pero la presencia de material del cromosoma Y aumenta el riesgo de desarrollar un tipo de cáncer llamado gonadoblastoma.</li>
                    </ul>
