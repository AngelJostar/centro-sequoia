                <p>Debido a que los <b> síntomas</b> varían, los <b> tratamientos </b> están diseñados para abordar los problemas de cada niña particularmente. Los tratamientos principales para casi todas las niñas y mujeres con síndrome de Turner incluyen terapias hormonales, estas son:</p>
            </div>
            <div class=" pt-15 justificar-texto-90  mt-2">
                <h3><b>Hormona del Crecimiento</b></h3>
            </div>
            <div class="justificar-texto-85 justificar-derecha-texto pt-15 ">
                <p>El déficit de  estatura en niñas con Síndrome de Turner es de aproximadamente 20 cm. La <b> estatura que las niñas </b> con Síndrome de Turner pueden ganar con tratamiento de <b> hormona del crecimiento  </b>depende de la edad a la que se inicie la terapia. La ganancia de altura esperada cuando se inicia tratamiento con <b> hormona del crecimiento </b> a los 12 años puede ser sólo de 5 cm, mientras que la ganancia de estatura con tratamiento comenzado a los 7 años o antes puede ser de 12 cm o más.</p>
            </div>
            <div class=" pt-15 justificar-texto-90  mt-2">
                <h3><b>Estrógeno</b></h3>
            </div>
            <div class="justificar-texto-85 justificar-derecha-texto pt-15 ">
                <p>La mayoría de las <b> niñas con síndrome de Turner</b> necesitan comenzar la <b> terapia con estrógenos </b> y hormonas relacionadas para comenzar la pubertad. A menudo, la terapia con estrógenos se inicia alrededor de los 11 o 12 años. El estrógeno ayuda a promover el desarrollo de los senos y mejora el tamaño del útero. El estrógeno ayuda con la mineralización ósea y, cuando se usa con la <b> hormona del crecimiento,</b> también puede ayudar con la altura. La terapia de reemplazo de estrógenos generalmente continúa durante toda la vida, hasta que se alcanza la edad promedio de la menopausia.</p>
