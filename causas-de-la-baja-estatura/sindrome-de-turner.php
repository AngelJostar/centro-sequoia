                       <li><h4>Amniocentesis. </h4> En esta prueba, se toma una muestra del líquido amniótico del útero. El bebé arroja células al líquido amniótico. El líquido se puede enviar al laboratorio de genética para estudiar los cromosomas del bebé en estas células.</li>
