            <div class="justificar-texto-85 justificar-derecha-texto mt-2 ">
                <p>El <b>Síndrome de Turner </b>es una afección médica que ocurre cuando falta uno de los cromosomas X. El <b>Síndrome de Turner</b> es un padecimiento que ocurre únicamente en mujeres. Este
                    síndrome causa diferentes afecciones médicas y del <a href="/crecimiento-infantil">desarrollo y crecimiento</a>, que incluyen <a href="/generalidades-del-crecimiento-infantil/baja-estatura"> baja estatura </a>, falla en el desarrollo de los ovarios y defectos cardíacos. </p>
                <p>El <b>Síndrome de Turner</b> se diagnostica en la niñez o desde el embarazo. A veces, en niñas con síntomas leves el diagnóstico se tarda más, pudiéndose diagnosticar hasta <a href="/crecimiento-infantil-por-edad/adolecentes-12-16"> la adolescencia</a></p>
                <p>Todas las niñas con <b>Síndrome de Turner</b> requieren atención médica de algún <a href="/especialistas-en-crecimiento/">endocrinólogo pediatra especialista en crecimiento</a>. Las consultas médicas y la atención de un especialista sin duda ayudan a la mayoría de las niñas a tener una vida sana y normal. En <a href="/index">Centro Sequoia</a> somos <b>especialistas en el tratamiento de niñas con Síndrome de Turner.</b></p>
            </div>
