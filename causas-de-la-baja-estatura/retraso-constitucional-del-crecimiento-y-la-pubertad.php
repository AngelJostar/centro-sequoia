<p><span style="font-weight: 400;">Algunos de estos resultados incluyen </span><b>mala cognición y rendimiento educativo</b><span style="font-weight: 400;">, pérdida de productividad y cuando se acompaña de un aumento de peso excesivo en la infancia, un mayor riesgo de </span><b>enfermedades crónicas</b><span style="font-weight: 400;"> relacionadas con la nutrición en la vida adulta.</span></p>
<p><span style="font-weight: 400;">El </span><b>retraso constitucional del crecimiento</b><span style="font-weight: 400;"> y el desarrollo deficientes que experimentan los niños es, generalmente, debido a una mala nutrición, infecciones repetidas y estimulación psicosocial inadecuada.</span></p>
<p><span style="font-weight: 400;">Los pequeños se definen como con retraso en el crecimiento </span><b>si su estatura para la edad está más de dos desviaciones estándar por debajo de la mediana</b><span style="font-weight: 400;"> de los patrones de crecimiento infantil de la Organización Mundial de la Salud (OMS).</span></p>
<p><b>&nbsp;</b></p>
<h2><span style="font-weight: 400;">Causas del </span><i><span style="font-weight: 400;">retraso en el crecimiento en niños</span></i><span style="font-weight: 400;"> y adolescentes</span></h2>
<p><span style="font-weight: 400;">El retraso en el crecimiento se asocia con </span><b>resultados desfavorecedores a corto, mediano y largo plazo</b><span style="font-weight: 400;"> en cinco puntos básicos:</span></p>
<ol>
<li><b></b><span style="font-weight: 400;"> Cambios fisiológicos que contribuyen a las enfermedades no transmisibles de los adultos y al aumento de la mortalidad.</span></li>
<li><b></b><span style="font-weight: 400;"> Reducción de la fuerza física y la capacidad de trabajo, lo que conduce a una reducción de los ingresos.</span></li>
<li><b></b><span style="font-weight: 400;"> Resultados de nacimiento indeseables en la próxima generación, es decir, los bebés de bajo peso al nacer o pequeños para la edad gestacional (PEG) tienen más probabilidades de morir o no crecer adecuadamente.</span></li>
<li><b></b><span style="font-weight: 400;"> Mayor riesgo de desproporción cefalopélvica, que conduce a distocia, mortalidad y morbilidad.</span></li>
<li><b></b><span style="font-weight: 400;"> Retraso en el desarrollo infantil, que conduce a un menor rendimiento escolar y menores ingresos.</span></li>
</ol>
<p><span style="font-weight: 400;">El retraso en el crecimiento puede tener una amplia variedad de causas. Entre las más comunes podemos mencionar:</span></p>
<p><b>Antecedentes familiares de baja estatura</b></p>
<p><span style="font-weight: 400;">Si los padres u otros </span><b>miembros de la familia tienen baja estatura</b><span style="font-weight: 400;">, es común que un niño crezca a un ritmo más lento que sus compañeros. El niño puede ser más bajo que el promedio simplemente debido a la </span><b>genética</b><span style="font-weight: 400;">. El retraso en el crecimiento debido a antecedentes familiares </span><b>no es una indicación de un problema de salud</b><span style="font-weight: 400;">.</span></p>
<p><b>Retraso constitucional del crecimiento</b></p>
<p><span style="font-weight: 400;">Los niños con esta afección </span><b>son más bajos que el promedio, pero crecen a un ritmo normal</b><span style="font-weight: 400;">. Generalmente, tienen una "edad ósea" más lenta, lo que significa que sus huesos maduran a un ritmo más desacelerado que su edad.</span></p>
<p><span style="font-weight: 400;">También tienden a llegar a la </span><b>pubertad retrasada</b><span style="font-weight: 400;"> en comparación con sus compañeros de clase, por ejemplo. Este hecho conlleva a una estatura por debajo del promedio en los primeros años de la adolescencia, pero tienden a alcanzar a sus similares cuando son adultos.</span></p>
<p><b>Deficiencia de la hormona del crecimiento</b></p>
<p><span style="font-weight: 400;">En circunstancias normales, la hormona del crecimiento promueve el desarrollo de los tejidos corporales. Los niños con</span><b> una deficiencia parcial o completa de la hormona del crecimiento no podrán mantener una tasa de crecimiento saludable</b><span style="font-weight: 400;">.</span></p>
<p><b>Hipotiroidismo</b></p>
<p><span style="font-weight: 400;">Los bebés o niños con hipotiroidismo tienen una glándula tiroides hipoactiva. La tiroides es responsable de liberar hormonas que promueven el crecimiento normal, por lo que </span><b>el retraso en el crecimiento es un posible signo de una tiroides hipoactiva.</b></p>
<h2><span style="font-weight: 400;">Consecuencias del </span><i><span style="font-weight: 400;">retraso en el crecimiento en niños</span></i></h2>
<p><span style="font-weight: 400;">Los niños que sufren de </span><b>retraso en el crecimiento</b><span style="font-weight: 400;"> o emaciación tienen más probabilidades de </span><b>sufrir problemas de salud</b><span style="font-weight: 400;"> y estar en riesgo de sufrir enfermedades y afecciones relacionadas con la dieta.</span></p>
<p><span style="font-weight: 400;">Pero el retraso del crecimiento no solo afecta la salud de un niño, sino que también inhibe su desarrollo futuro como en los siguientes aspectos:</span></p>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Los niños con retraso del crecimiento ganan un 20% menos como adultos que sus contrapartes sin retraso del crecimiento.</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Es posible que los niños que sufren de retraso del crecimiento </span><b>nunca lleguen a alcanzar su estatura completa</b><span style="font-weight: 400;"> ni desarrollen todo su potencial cognitivo.</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Las madres afectadas por la desnutrición tienen </span><b>más probabilidades de tener hijos que sufren de retraso en el crecimiento</b><span style="font-weight: 400;">, lo que perpetúa el ciclo de pobreza y desnutrición.</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">El 43% de los niños menores de cinco años en países de ingresos bajos y medianos tienen un </span><b>riesgo elevado de pobreza debido al retraso en el crecimiento</b><span style="font-weight: 400;">.</span></li>
</ul>
