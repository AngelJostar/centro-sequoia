        <h4 id="2.1.2">Desarrollo cognitivo de 0 a 2 años de edad</h4>
        <p>Durante el primer año, los <strong>bebés</strong> aprenden a enfocar su visión, extender la mano, explorar y aprender sobre las cosas que los rodean. El <strong>desarrollo cognitivo</strong> o cerebral significa el proceso de aprendizaje de la memoria, el lenguaje, el pensamiento y el razonamiento. Aprender el idioma es más que hacer sonidos ("balbuceo") o decir "ma-ma" y "pa-pa". Escuchar, comprender y conocer los nombres de personas y cosas son parte del desarrollo del lenguaje.</p>
        <p>Durante el <strong>segundo año</strong> se mueven más y son conscientes de sí mismos y de su entorno. el deseo de explorar nuevos objetos y personas también está aumentando. </p>
        <p>Durante esta etapa, mostrarán una mayor independencia; comienzan a mostrar un comportamiento desafiante; reconocerse en imágenes o en un espejo; e imitar el comportamiento de los demás, especialmente los adultos y chavos mayores. Los niños pequeños también deberian poder reconocer los nombres de personas y objetos familiares, componer frases y oraciones simples y seguir instrucciones e indicaciones simples.</p>
        <h4 id="2.1.3">Desarrollo social y emocional de 0 a 2 años de edad</h4>
        <p>La primera relación del <strong>bebé</strong> es generalmente con miembros de la <strong>familia</strong>, a quienes el bebé expresa una variedad de emociones y viceversa. Si el vínculo <strong>social y emocional</strong> entre el bebé y la familia es defectuoso de alguna manera, es posible que el niño nunca desarrolle la confianza, el autocontrol o el razonamiento emocional necesarios para funcionar eficazmente en el mundo. </p>


        <h3 id="2.2">Crecimiento y desarrollo en la primera infancia (3 a 6 años)</h3>
        <h4 id="2.2.1">Crecimiento físico de los 3 a los 6 años de edad</h4>
        <p>Comienzan a perder la grasa de bebé alrededor de los 3 años. Los <strong>niños pequeños</strong> pronto adquieren el aspecto más delgado y atlético asociado con la <strong>infancia</strong>. El tronco y las extremidades del niño se alargan y se forman los músculos abdominales, lo que contrae la apariencia del estómago. Incluso en esta etapa temprana de la vida, tienden a tener más masa muscular que las niñas. </p>
        <p>Las proporciones físicas de los niños en edad preescolar también continúan cambiando, y sus cabezas siguen siendo desproporcionadamente grandes, pero menos que en la infancia.</p>
        <p>Los niños en <strong>edad preescolar</strong> de tres años pueden llegar a medir aproximadamente 97 centímetros de <strong>alto</strong> y pesar alrededor de 15 kilos. Durante los próximos 3 años, crecen de 5 a 10 centímetros adicionales y aumentan de 2 a 3 kilos por año. A los 6 años, alcanzan una altura de aproximadamente 116 centímetros y pesan alrededor de 20 kilos. Por supuesto, estas cifras <a href="/aprende-del-crecimiento-infantil/graficas-de-crecimiento/">son promedios y difieren de un niño a otro</a>, según el nivel socioeconómico, la alimentación, la <a href="/factores-que-perjudican-el-crecimiento/padecimientos-que-afectan-el-crecimiento/">salud</a> y los factores hereditarios.</p>
        <h4 id="2.2.2">Desarrollo cognitivo de los 3 a 6 años de edad</h4>
        <p>A esta <strong>edad</strong> los niños juegan un papel importante en su propio <strong>desarrollo cognitivo</strong>, especialmente en sus intentos de comprender, explicar, organizar, manipular y construir. Los pequeños también ven patrones en objetos y eventos del mundo y luego intentan organizar esos patrones para explicar el mundo.</p>
        <p>Al mismo tiempo, los pequeños en <strong>edad preescolar</strong> tienen limitaciones <strong>cognitivas</strong>. Tienen problemas para controlar su atención y memoria, confunden las apariencias superficiales con la realidad y se concentran en un solo aspecto de una experiencia a la vez. </p>
        <p>En esta etapa, </p>
        <ul>
            <li>Aumentan su uso del lenguaje y otros símbolos como el juego. </li>
            <li>Los pequeños desarrollan una fascinación por las palabras </li>
            <li>A esta edad también juegan juegos de fantasía: usan una caja vacía como coche, juegan en familia con hermanos y cultivan amistades imaginarias.</li>
        </ul>
        <h4 id="2.2.3">Desarrollo social y emocional de los 3 a 6 años de edad</h4>
        <p>Los <strong>vínculos familiares</strong> tempranos pueden determinar la facilidad con la que los niños forman amistades y otras relaciones. Los niños que tienen relaciones amorosas, estables y de aceptación con sus padres y hermanos generalmente tienen más probabilidades de establecer relaciones similares con amigos y compañeros de juego. </p>
        <p>Las primeras <strong>amistades</strong> se crean cuando un niño tiene aproximadamente 3 años, aunque en <strong>edad preescolar</strong> pueden jugar juntos antes de esa edad. Al igual que los adultos, los niños en edad preescolar tienden a desarrollar amistades con niños que comparten intereses comunes, son agradables, ofrecen apoyo y son similares en tamaño y apariencia. </p>
        <p>Las amistades de la infancia crean oportunidades para que los niños aprendan a manejar situaciones que provocan ira, a compartir, a aprender valores y a practicar comportamientos más maduros. Los niños en edad preescolar que son populares entre sus compañeros se destacan en estas actividades. Estos niños saben cómo ser amigos, no solo cómo tener amigos. Por otro lado, los niños que “chismorrean” u hostilidad directa hacia sus compañeros de juego tienden a ser menos populares. A su vez, los niños agresivos suelen tener menos amigos, lo que alimenta aún más su hostilidad.</p>


        <h3 id="2.3">Crecimiento y desarrollo en la segunda infancia (6 a 11 años</h3>
        <h4 id="2.3.1">Crecimiento físico de los 6 a los 11 años de edad</h4>
        <p>A medida que los niños crecen desde el inicio de la escuela primaria hasta la secundaria, tienden a <a href="/aprende-del-crecimiento-infantil/graficas-de-crecimiento/">crecer a un ritmo</a> bastante constante, creciendo alrededor de 6 a 7 centímetros cada año. En cuanto a su peso, aumentan entre 2 y 3 kilos por año hasta que comience la pubertad.</p>
        <p>Este es un momento en el que comienzan a tener sentimientos  <a href="/aprende-del-crecimiento-infantil/etapas-del-desarollo-de-tanner/">sobre cómo se ven</a> y cómo están creciendo. Algunas niñas pueden preocuparse por ser "demasiado grandes", especialmente aquellas que se <a href="/padecimentos-que-afectan-el-crecimiento/pubertad-precoz/">desarrollan temprano</a>. Los niños tienden a ser sensibles por ser demasiados chaparros.</p>
        <p>Nuestros hijos no pueden cambiar los <a href="/aprende-del-crecimiento-infantil/talla-blanco-familiar/">genes</a> que determinarán su estatura o cuándo comienza la <a href="/cambios-en-la-adolescencia/">pubertad</a>. Pero pueden aprovechar al máximo su potencial desarrollando <a href="/generalidades-del-crecimiento-infantil/nutricion-malnutricion-y-crecimiento/">hábitos alimenticios</a> saludables y siendo físicamente activos.</p>
        <p>Para saber si tu hijo está creciendo como debería, tu médico tomará medidas en las consultas médicas regulares, luego trazará las medidas (de estatura y peso) de tu hijo en una <a href="/aprende-del-crecimiento-infantil/graficas-de-crecimiento/">tabla de crecimiento estándar</a> para seguir a lo largo del tiempo y compararlos con otros niños de la misma edad y sexo.</p>
