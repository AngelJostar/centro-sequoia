        <h3 id="2.1">Crecimiento y desarrollo en la niñez (0 a 2 años)</h3>
        <h4 id="2.1.1">Crecimiento físico de 0 a los 2 años de edad</h4>
        <p>Un <strong>bebe</strong> recién nacido promedio pesa aproximadamente 3,500 gramos, aunque se considera que un peso saludable al nacer para un bebé a término es entre 2,500 gramos y 4,000 gramos. La longitud promedio de un recién nacido es de 25 centímetros, aumentando a 75 centímetros a los 12 meses y 87 centímetros a los 2 años.</p>
        <p>La <strong>longitud de los bebés</strong> aumenta alrededor del 30% a los 5 meses y más del 50% a los 12 meses. Su altura a los 5 años es aproximadamente el doble de la longitud al nacer. En los niños, la mitad de la estatura adulta se alcanza alrededor de los 2 años. En las niñas, la estatura a los 19 meses es aproximadamente la mitad de la estatura adulta.</p>
