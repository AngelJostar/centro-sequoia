        <h2>Introducción al crecimiento y desarrollo de tu hijo</h2>
        <p>Tú quieres lo mejor para tus hijos, y todos los padres quieren. Pero ser mama o papa puede ser abrumador. Además de los problemas normales que pueda tener un <strong>niño</strong>, a muchos padres les preocupa que sus hijos estén <strong>creciendo y desarrollándose</strong> como deberían.</p>
        <p>El <strong>crecimiento y desarrollo</strong> de tus hijos es multifactorial. Hay tres factores que lo componen:</p>
        <ul>
            <li><strong>Crecimiento físico</strong>: Incluye el <strong>crecimiento de estatura</strong> y <strong>aumento de peso</strong>. También el mejoramiento de las habilidades motrices y sensoriales.</li>
            <li><strong>Desarrollo cognitivo</strong>: Son cambios en la inteligencia, el lenguaje, la memoria, la percepción y la resolución de problemas.</li>
            <li><strong>Desarrollo social y emocional</strong>: Son cambios en las emociones, la autoestima y las relaciones con familiares y amigos.</li>
        </ul>
        <p>Estos tres factores interactúan entre sí para lograr que tu hijo o hija crezca y se desarrolle hasta llegar a ser un adulto pleno. </p>
        <p>Tus hijos pueden enfrentar <a href="/factores-que-perjudican-el-crecimiento/">dificultades</a> a medida que crecen. Es muy importante que tú como mamá o papá comprendas los cambios que tus hijos encontraran durante su <strong>crecimiento y desarrollo</strong> para poder brindarle el cuidado y atención que necesita.</p>
        <h2>Crecimiento y desarrollo por etapas</h2>
        <p>El <strong>crecimiento y desarrollo de los niños</strong> se puede dividir en cinco etapas distintas, que incluyen:</p>
        <ul>
            <li>Desarrollo prenatal (ocurre desde concepción hasta el nacimiento)</li>
            <li><strong>Infancia y niñez</strong>: recién nacido y bebé (0 a 2 años)</li>
            <li><strong>Primera infancia</strong>: niño pequeño en edad preescolar (3 a 5 años) </li>
            <li><strong>Segunda infancia</strong>: niño en edad escolar (6 a 11 años)</li>
            <li><strong>Adolescencia</strong>: etapa de la adolescencia (12 en adelante). Nota: este tema lo exponemos a fondo en la sección <a href="/cambios-en-la-adolescencia/">“Cambios en la adolescencia”</a> de nuestro sitio web. Para más información sobre el crecimiento y desarrollo de adolescentes da clic <a href="/cambios-en-la-adolescencia/">AQUÍ</a>.</li>
        </ul>
        <h3>Crecimiento y desarrollo en la segunda infancia (6 a 11 años</h3>
        <h4>Crecimiento físico de los 6 a los 11 años de edad</h4>
        <p>A medida que los niños crecen desde el inicio de la escuela primaria hasta la secundaria, tienden a <a href="/aprende-del-crecimiento-infantil/graficas-de-crecimiento/">crecer a un ritmo</a> bastante constante, creciendo alrededor de 6 a 7 centímetros cada año. En cuanto a su peso, aumentan entre 2 y 3 kilos por año hasta que comience la pubertad.</p>
        <p>Este es un momento en el que comienzan a tener sentimientos  <a href="/aprende-del-crecimiento-infantil/etapas-del-desarollo-de-tanner/">sobre cómo se ven</a> y cómo están creciendo. Algunas niñas pueden preocuparse por ser "demasiado grandes", especialmente aquellas que se <a href="/padecimentos-que-afectan-el-crecimiento/pubertad-precoz/">desarrollan temprano</a>. Los niños tienden a ser sensibles por ser demasiados chaparros.</p>
        <p>Nuestros hijos no pueden cambiar los <a href="/aprende-del-crecimiento-infantil/talla-blanco-familiar/">genes</a> que determinarán su estatura o cuándo comienza la <a href="/cambios-en-la-adolescencia/">pubertad</a>. Pero pueden aprovechar al máximo su potencial desarrollando <a href="/generalidades-del-crecimiento-infantil/nutricion-malnutricion-y-crecimiento/">hábitos alimenticios</a> saludables y siendo físicamente activos.</p>
        <p>Para saber si tu hijo está creciendo como debería, tu médico tomará medidas en las consultas médicas regulares, luego trazará las medidas (de estatura y peso) de tu hijo en una <a href="/aprende-del-crecimiento-infantil/graficas-de-crecimiento/">tabla de crecimiento estándar</a> para seguir a lo largo del tiempo y compararlos con otros niños de la misma edad y sexo.</p>
