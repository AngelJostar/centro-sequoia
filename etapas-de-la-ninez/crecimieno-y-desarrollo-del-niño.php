        <h4 id="2.3.2">Desarrollo cognitivo de los 6 a los 11 años de edad</h4>
        <p>Los <strong>cambios cognitivos</strong> que experimentan a esta edad son a menudo más notables que sus cambios físicos.</p>
        <p>A esta edad comprenden conceptos como pasado, presente y futuro, lo que les da la capacidad de planificar y trabajar para alcanzar metas. Además, pueden procesar ideas complejas como sumas y restas y relaciones de causa y efecto. Algunos de los cambios cognitivos básicos a esta edad son:</p>
        <ol>
            <li>La capacidad para elegir de forma consciente, reflexiva y proactiva.</li>
            <li>Perseguir objetivos en lugar de simplemente reaccionar al entorno.</li>
            <li>El pensamiento se vuelve más lógico, organizado y flexible.</li>
            <li>Aumenta su capacidad de clasificar las cosas simultáneamente en grupos generales y más específicos, utilizando diferentes tipos de comparaciones.</li>
            <li>Se vuelven capaces de discriminar objetos cercanos y lejanos en función de su tamaño aparente.</li>
            <li>Su capacidad de atención se alarga en duración y su capacidad para enfocar y concentrar su atención se vuelve más pronunciada y confiable.</li>
            <li>La capacidad de memoria y la habilidad de usar su memoria también aumentan y mejoran.</li>
        </ol>
        <h4 id="2.3.3">Desarrollo social y emocional de los 6 a los 11 años de edad</h4>
        <p>En esta edad logran grandes avances en términos de su capacidad para reconocer las emociones en sí mismos y en los demás, controlar sus propias emociones y comunicarse sobre las emociones, tanto de manera expresiva como con el lenguaje.</p>
        <ul>
            <li>Aumentan sus habilidades para tomar perspectiva y sus habilidades de empatía.</li>
            <li>El mayor interés en las relaciones con sus compañeros y familiares los hace sensibles a las emociones autoconscientes de orgullo, culpa y vergüenza.</li>
            <li>La identidad personal se desarrolla para volverse más compleja.</li>
            <li>La autoestima general puede fluctuar o disminuir a medida que inician el proceso de comparación social con sus compañeros.</li>
            <li>Las amistades entre compañeros adquieren un papel más destacado que nunca.</li>
            <li>Mejoran en la lectura de las emociones de otros niños y comprenden las intenciones y necesidades de otros niños.</li>
        </ul>
        <p>Aunque las relaciones de los niños con los compañeros maduran y se vuelven cada vez más prominentes durante la niñez, la conexión de los niños con sus padres y su núcleo familiar sigue siendo de tremenda importancia para su bienestar y funcionamiento.</p>






        <h2 id="3">¿Cuándo visitar un médico?</h2>
        <p>Llame a un <a href="/especialistas-en-crecimiento/">médico especialista</a> de <a href="https://centrosequoia.com.mx/">Centro Sequoia</a> en cualquier momento que tenga alguna inquietud sobre la salud física o emocional de su hijo. Asegúrese de llamarnos si su hijo:</p>
        <ul>
            <li>No está alcanzando los hitos del desarrollo físico como se esperaba.</li>
            <li>No está creciendo a un ritmo constante.</li>
            <li>Si usted desea que su hijo alcance una mayor estatura.	</li>
        </ul>
