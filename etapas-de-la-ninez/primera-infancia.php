        <h4>Habilidades a los 5 años</h4>
        <ul>
            <li>Salta la cuerda.</li>
            <li>Camina hacia atrás.</li>
            <li>Se equilibra en un pie con los ojos cerrados.</li>
            <li>Usa tijeras.</li>
            <li>Empieza a aprender a atarse los zapatos.</li>
            <li>Copia formas mientras dibuja.</li>
            <li>Se viste solo.</li>
            <li>Conoce su dirección y número de teléfono.</li>
            <li>Reconoce y recita el alfabeto.</li>
        </ul>
        <h4>Habilidades de los 6 a los 7 años</h4>
        <ul>
            <li>Disfruta de muchas actividades y se mantiene ocupado.</li>
            <li>Le gusta pintar y dibujar.</li>
            <li>Practica habilidades para mejorar.</li>
            <li>Salta la cuerda.</li>
            <li>Montas en bicicleta.</li>
        </ul>
        <h4>Habilidades de los 8 a los 9 años</h4>
        <ul>
            <li>Es más elegante con los movimientos y las habilidades.</li>
            <li>Saltos, saltos y persecuciones.</li>
            <li>Se viste y se casa completamente.</li>
            <li>Puede usar herramientas, como un martillo o un destornillador.</li>
        </ul>
        <h3 id="1.4">Desarrollo cognitivo en la primera infancia</h3>
        <p>Los <strong>cambios físicos</strong> y motrices en la primera infancia se acompañan de cambios en el
        desarrollo cognitivo. El <strong>desarrollo cognitivo</strong> es la capacidad de los niños de razonar y
        pensar.</p>
        <p>Todos los niños en la primera infancia pueden progresar a diferentes tiempos, pero aquí
        encontraras algunos de los avances comunes que su hijo puede alcanzar en la <strong>primera
        infancia:</strong></p>
        <h4>Desarrollo cognitivo a los 3 años</h4>
        <ul>
            <li>Entiende las diferencias de tamaño, como grandes y pequeños.</li>
            <li>Entiende el tiempo pasado (ayer).</li>
            <li>Entiende oraciones largas.</li>
            <li>Entiende las preposiciones (encima, debajo, detrás).</li>
            <li>Usa pronombres correctamente como, yo, tú, él y yo.</li>
            <li>Pregunta "por qué" constantemente.</li>
            <li>Cuenta hasta 4 objetos.</li>
        </ul>
        <h4>Desarrollo cognitivo a los 4 años</h4>
        <ul>
            <li>Dice el nombre completo y la edad.</li>
            <li>Puede tener miedo a ciertas cosas. Por ejemplo, oscuro, monstruo debajo de la
                cama y yendo por el desagüe.</li>
            <li>Intentos de resolver problemas</li>
            <li>Recuerda ciertos eventos</li>
            <li>Puede señalar la imagen correcta cuando se le hace una simple pregunta al
                respecto.</li>
            <li>Empieza a entender el tiempo.</li>
            <li>Comienza a ser más consciente de las personas que lo rodean.</li>
            <li>Puede obedecer las reglas de los padres, pero no entiende el bien del mal.</li>
            <li>Cree que sus propios pensamientos pueden hacer que las cosas sucedan.</li>
        </ul>
        <h4>Desarrollo cognitivo a los 5 años</h4>
        <ul>
            <li>Tiene más comprensión del tiempo.</li>
            <li>Tiene curiosidad por los hechos reales sobre el mundo.</li>
            <li>Puede comparar las reglas de los padres con las de los amigos.</li>
        </ul>
        <h4>Desarrollo cognitivo a de los 6 a 7 años</h4>
        <ul>
            <li>Entiende el concepto de números.</li>
            <li>Conoce el día y la noche.</li>
            <li>Conoce la mano derecha e izquierda.</li>
            <li>Puede copiar formas complejas, como un diamante.</li>
            <li>Puede decir la hora.</li>
            <li>Entiende los comandos que tienen 3 instrucciones separadas.</li>
            <li>Puede explicar los objetos y su uso.</li>
            <li>Puede repetir 3 números al revés.</li>
            <li>Puede leer libros apropiados para su edad.</li>
        </ul>
        <h4>Desarrollo cognitivo a de los 8 a 9 años</h4>
        <ul>
            <li>Puede contar hacia atrás.</li>
            <li>Sabe la fecha.</li>
            <li>Lee más y disfruta leyendo.</li>
            <li>Entiende fracciones.</li>
            <li>Entiende el concepto de espacio.</li>
            <li>Dibujos y pinturas.</li>
            <li>Puede nombrar los meses y días de la semana, en orden.</li>
            <li>Disfruta coleccionando objetos.</li>
        </ul>
        <h3 id="1.5">Desarrollo del lenguaje en la primera infancia</h3>
        <p>El lenguaje es una herramienta poderosa para mejorar el desarrollo cognitivo en la
        primera infancia. El uso del lenguaje le permite al niño comunicarse con los demás y
        resolver problemas.</p>
        <p>El desarrollo del habla es muy emocionante para los padres, ya que ven a sus hijos
        comenzar a hablar con claridad e interactuar con los demás. Es importante estar al
        pendiente del desarrollo del habla en los niños durante la primera infancia. A continuación
        te presentamos todo lo que debes saber.</p>
        <p>Cada niño desarrolla el habla a su propio ritmo, pero estos son algunos de los hitos
        comunes en este grupo de edad:</p>
        <h4>Lenguaje a los 3 años</h4>
        <p>Durante los primeros tres años de vida, los niños desarrollan un vocabulario hablado de
        entre 300 y 1000 palabras, y pueden usar el lenguaje para aprender y describir el mundo
        que los rodea.</p>
        <ul>
            <li>Debería poder decir entre 500 y 900 palabras.</li>
            <li>Los demás pueden entender el habla.</li>
            <li>Habla en oraciones de 2 o 3 palabras y progresa a oraciones de 4 o 5 palabras.</li>
            <li>Puede recordar rimas o letras simples.</li>
            <li>Utiliza "por favor" y "gracias".</li>
            <li>Se refiere a sí mismo usando su propio nombre.</li>
            <li>Nombres colores.</li>
        </ul>
        <h4>Lenguaje a los 4 años</h4>
        <ul>
            <li>Puede juntar de 4 a 5 palabras en una oración.</li>
            <li>Hará preguntas constantemente.</li>
            <li>Puede conocer 1 o más colores.</li>
            <li>Le gusta contar historias.</li>
            <li>Puede usar algunas malas palabras (si las ha escuchado repetidamente).</li>
        </ul>
        <h4>Lenguaje a los 5 años</h4>
        <p>A los cinco años, el <strong>lenguaje</strong> de un niño crecerá a aproximadamente 1.500 palabras. Los
        niños de cinco años también pueden producir oraciones de cinco a siete palabras,
        aprender a usar el tiempo pasado y contar historias familiares utilizando imágenes como
        pistas.</p>
        <ul>
            <li>Puede juntar de 6 a 8 palabras en una oración.</li>
            <li>Puede conocer 4 o más colore.</li>
            <li>Conoce los días de la semana y los meses.</li>
            <li>Puede nombrar monedas y dinero.</li>
            <li>Puede entender comandos con múltiples instrucciones.</li>
            <li>Habla a menudo.</li>
        </ul>
        <h4>Lenguaje De los 6 a 8</h4>
        <p>A los ocho años, los niños pueden demostrar una comprensión básica de conceptos
        menos concretos, incluido el tiempo y el dinero. Sin embargo, el niño de ocho años
        todavía razona de manera concreta y tiene dificultades para comprender ideas abstractas.</p>
        <h3 id="1.6">Desarrollo socioemocional en la primera infancia</h3>
        <p>Una parte importante del crecimiento en la primera infancia es aprender a interactuar y
        socializar con los demás. Esta puede ser una transición frustrante para los padres. Los
        niños pasan por diferentes etapas. Algunos de estos no siempre son fáciles de manejar.</p>
        <p>Cada niño es único y desarrollará diferentes personalidades en la primera infancia, pero
        estos son algunos de los rasgos de comportamiento comunes que pueden estar
        presentes en su hijo:</p>
        <h4>Desarrollo socioemocional a los 3 años</h4>
        <ul>
            <li>Empieza a compartir y le gusta jugar con otros niños.</li>
            <li>Puede tomar turnos.</li>
            <li>Las rabietas son menos frecuentes.</li>
            <li>Empieza a mostrar sentimientos de formas socialmente aceptables.</li>
        </ul>
        <h4>Desarrollo socioemocional a los 4 años</h4>
        <ul>
            <li>Es muy independiente, quiere hacer las cosas por su cuenta.</li>
            <li>Es egoísta y no le gusta compartir.</li>
            <li>Está de mal humor. Los cambios de humor son comunes.</li>
            <li>Puede ser agresivo durante los cambios de humor y volverse agresivo con los
                miembros de la familia.</li>
            <li>Tiene varios miedos.</li>
            <li>Puede tener amigos imaginarios.</li>
            <li>Le gusta explorar el cuerpo y puede jugar al proveedor de atención médica
                Podría "huir" o amenazar con hacerlo.</li>
            <li>Peleas con hermanos.</li>
            <li>A menudo jugará con otros en grupos.</li>
        </ul>
        <h4>Desarrollo socioemocional a los 5 años</h4>
        <ul>
            <li>Generalmente es más cooperativo que un niño de 4 años.</li>
            <li>Generalmente es más responsable que un niño de 4 años.</li>
            <li>Está ansioso por complacer a los demás y hacerlos felices.</li>
            <li>Tiene buenos modales.</li>
            <li>Se viste completamente sin ayuda.</li>
            <li>Se lleva bien con los padres.</li>
            <li>Le gusta cocinar y hacer deporte.</li>
            <li>Puede volverse más apegado a un padre a medida que comienza a asistir a la
                escuela.</li>
        </ul>
        <h4>Desarrollo socioemocional de los 6 a los 7 años</h4>
        <p>Una parte importante del crecimiento en la <strong>primera infancia</strong> es aprender a interactuar y
        socializar con los demás. Durante los años de edad escolar, verá un cambio en su hijo.
        Pasará de jugar solo a tener varios amigos y grupos sociales.</p>
        <p>Las amistades se vuelven más importantes. Pero su hijo todavía los quiere como padres y
        le gusta ser parte de una familia. A continuación, se muestran algunos de los rasgos
        comunes que su hijo puede mostrar a estas edades.</p>
        <ul>
            <li>Coopera y comparte.</li>
            <li>Puede tener celos de otros y hermanos.</li>
            <li>Le gusta copiar a los adultos.</li>
            <li>Le gusta jugar solo, pero los amigos se están volviendo importantes.</li>
            <li>Juega con amigos del mismo sexo.</li>
            <li>A veces puede tener rabietas.</li>
            <li>Es modesto sobre su cuerpo.</li>
            <li>Le gusta jugar juegos de mesa.</li>
        </ul>
        <h4>Desarrollo socioemocional de los 8 a los 9 años</h4>
        <ul>
            <li>Le gusta la competencia y los juegos</li>
            <li>Empieza a mezclar amigos y a jugar con niños del sexo opuesto.</li>
            <li>Es modesto sobre su cuerpo.</li>
            <li>Disfruta de clubes y grupos, como Boy Scouts o Girl Scouts.</li>
            <li>Se está interesando en las relaciones entre chicos y chicas, pero no lo admite.</li>
