        </div>
        <p>El crecimiento <a href="/cambios-en-la-adolescencia">prepuberal</a> es un proceso no lineal, que
        consiste en brotes de crecimiento que duran un promedio de ocho semanas, separados
        por períodos de crecimiento lento, que duran un promedio de 18 días. El crecimiento en la
        primera infancia es diferente al crecimiento en la adolescencia, por eso es importante
        estar al pendiente de este cambio de etapa.</p>
        <h3 id="1.3">Aumento de peso en la primera infancia</h3>
        <h4>Aumento de peso en niños < 2 años</h4>
        <p>Hitos típicos: las pautas generales con respecto al <strong>aumento de peso</strong> durante la <strong>primera
        infancia</strong> y la niñez <strong>para bebés</strong> con parámetros de crecimiento que eran apropiados para
        la edad gestacional al nacer incluyen lo siguiente:</p>
        <ul>
            <li>Los recién nacidos a término pueden perder hasta un 10 por ciento de su peso al
            nacer en los primeros días de vida y, por lo general, recuperar su peso al nacer
            entre los 10 y 14 días.</li>
            <li>Los recién nacidos aumentan aproximadamente 30 g por día hasta los tres meses
            de edad.</li>
            <li>Los bebés aumentan aproximadamente 20 g por día entre los tres y seis meses de
            edad y aproximadamente 10 g por día entre los 6 y los 12 meses.</li>
            <li>Los bebés duplican su peso al nacer a los cuatro meses de edad y triplican su
            peso al nacer al año.</li>
            <li>Los patrones de crecimiento de los lactantes prematuros, los lactantes pequeños para la
            edad gestacional y los lactantes grandes para la edad gestacional se analizan por
            separado.</li>
        </ul>
        <h4>Aumento de peso en niños > 3 años</h4>
        <p>A medida que avanzan en esta etapa durante la <strong>primera infancia</strong>, los niños comienzan a
        perder parte de la grasa que tenían de bebés. Esto hace que se vean más como niños y
        menos como bebes.</p>
        <p>Los niños generalmente aumentan aproximadamente 2 kg por año entre los dos años y la
        pubertad.</p>
        <p>Un niño prepúberto cuya velocidad de peso es < 1 kg por año debe ser monitoreado de
        cerca para detectar déficits nutricionales progresivos.</p>
        <div class="tamaño-img">
            <img loading="lazy" src="/img/etapaz-de-la-ninez/tabla-de-habilidades-motoras-en-ninos.webp" title="Tabla de habilidades motoras finas y gruesas en niños" alt="Tabla comparativa de las habilidades motoras finas y gruesas que debe de tener un niño según su edad">
        </div>
        <p>A medida que su hijo continúe creciendo durante la primera infancia, notará que se
        desarrollan nuevas y emocionantes habilidades. Los niños pueden progresar a diferentes
        ritmos, pero estos son algunos de los hitos comunes que su hijo puede alcanzar en este
        grupo de edad:</p>
        <h4>Habilidades a los 3 años</h4>
        <ul>
            <li>Corre y salta con facilidad.</li>
            <li>Sube escaleras sin ayuda.</li>
            <li>Monta un triciclo.</li>
            <li>Se lava y se seca las manos.</li>
            <li>Apila 10 bloques.</li>
            <li>Dibuja fácilmente líneas rectas y copia un círculo.</li>
            <li>Puede ponerse de puntillas.</li>
            <li>Usa bien la cuchara y se alimenta solo.</li>
            <li>Se viste y se desnuda solo a excepción de botones y cordones.</li>
            <li>Puede concentrarse en tareas durante 8 o 9 minutos.</li>
            <li>Tiene los 20 dientes primarios ("de leche").</li>
            <li>La visión se acerca a 20/20.</li>
            <li>Por lo general, se establece el control de la vejiga y los intestinos; usa orinal o
            inodoro.</li>
            <li>Puede dormir de 11 a 13 horas en total, aún puede tomar una pequeña siesta por
            la tarde.</li>
        </ul>
        <h4>Habilidades a los 4 años</h4>
        <ul>
            <li>Canta una canción.</li>
            <li>Salta y salta en un pie.</li>
            <li>Atrapa y lanza una pelota por encima de la cabeza.</li>
            <li>Camina escaleras abajo solo.</li>
            <li>Dibuja una persona con 3 partes del cuerpo separadas.</li>
            <li>Construye una torre de bloques con 10 bloques.</li>
            <li>Entiende la diferencia entre fantasía y realidad.</li>
        </ul>
