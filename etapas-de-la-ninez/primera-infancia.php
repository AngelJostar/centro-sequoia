        </div>
        <p>Las primeras experiencias de tu hijo, los vínculos que forma contigo como su <strong>padre o
        madre</strong> y sus primeros aprendizajes educativos, afectan su <a href="/etapas-de-la-ninez/cambios-fisicos-de-un-nino">desarrollo físico</a>, cognitivo, emocional y social para el futuro.</p>
        <p>Apuntalar los primeros años en la vida de tus hijos es la mejor inversión que puedes para
        asegurar su éxito futuro.</p>
        <h3 id="1.2">Crecimiento de estatura en la primera infancia</h3>
        <p>Los niños en la <strong>primera infancia</strong> (de 3 a 8 años de edad) crecen un ritmo muy rápido.</p>
        <p>Las proporciones corporales cambian quitando las características físicas particulares de
        los bebes. Un ejemplo muy notorio es la proporción de la cabeza que en bebes representa
        casi una cuarta parte de la longitud total del cuerpo y después disminuye en el niño
        pequeño con una apariencia más equilibrada y parecida a la de un adulto.</p>
        <p>Una tasa de <strong>crecimiento</strong> normal de un niño en la <strong>primera infancia</strong> es aquella que sigue
        los <a href="/aprende-del-crecimiento-infantil/graficas-de-crecimiento">percentiles de crecimiento</a> estandarizadas utilizadas por
        los <a href="/especialistas-en-crecimiento">médicos endocrinólogos pediatras</a></p>
        <!-- BANNER A GRAFICAS DE CRECIMIENTO -->
        <p>Medir regularmente la <strong>estatura y peso</strong> de tu hijo y marcarlas en una <strong>tabla de
        crecimiento</strong> es una buena forma de ver si su crecimiento está bien. En las visitas al
        pediatra, <a href="/especialistas-en-crecimiento">médico</a> tomara estas mismas medidas y
        las usara para evaluar el <a href="/etapas-de-la-ninez/cambios-fisicos-de-un-nino">desarrollo físico</a> de su hijo para
        asegurarse de que este creciendo como debería.</p>
        <p>Las pautas generales con respecto al aumento de longitud o estatura durante la primera
        infancia y la niñez incluyen las siguientes:</p>
        <!-- IMAGEN DE GRAFICAS -->
        <ul>
            <li>La longitud promedio al nacer de un bebé a término es de 50 cm.</li>
            <li>Los bebés crecen 25 cm durante el primer año de vida.</li>
            <li>Los niños pequeños crecen 10 cm entre los 12 y 24 meses, 7.5 cm entre los 24 y
            36 meses y 7.5 cm entre los 36 y 48 meses.</li>
            <li>Los niños alcanzan la mitad de su estatura adulta entre los 24 y 30 meses.</li>
            <li>Los niños crecen 5 centímetros por año entre los cuatro años y la pubertad.</li>
            <li>Hay una desaceleración normal de la velocidad de la altura antes del brote de
            crecimiento puberal.</li>
        </ul>
        <div class="size-img-resposive">
