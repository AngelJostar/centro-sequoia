        <h4>¿Qué pasa si no sube de peso lo suficiente o sube demasiado?</h4>
        <p>Si tu bebé no está subiendo los gramos normales de semana a semana entonces puede ser
        una de múltiples razones, siempre recomendamos checar primero con tu doctor antes de hacer
        cualquier cambio en la alimentación y cuidado de tu bebé.</p>
        <p><strong>Si no sube de peso:</strong></p>
        <p><strong>No está comiendo lo suficiente:</strong> lo normal es que los bebés coman entre 8 a 10 veces al día,
        intenta no dejar pasar más de 3 horas sin darle de comer durante el día y 4 durante la noche.</p>
        <p><strong>No esta drenando toda la leche del pecho:</strong> al menos debería de vaciar un pecho por
        completo de leche en cada alimentada, intenta que coma 10 a 15 minutos de un pecho antes
        de ofrecerle el otro.</p>
        <p><strong>Problemas motores:</strong> cabe la posibilidad de que aún no este coordinando los músculos de su
        mandíbula y por lo tanto le esté costando trabajo succionar leche del pecho o de la mamila lo
        que hace que el bebé se canse y frustre al momento de comer.</p>
        <p><strong>No produces suficiente leche:</strong> las razones pueden ser muchas pero estas son algunas de las
        más comunes; el dormir boca abajo le pone presión a los pechos por lo tanto imposibilitando la
        producción de leche, no comer lo suficiente, estar extremadamente cansada, regresar al trabajo
        y ya no dar de comer en las noches. La producción de leche materna está basada en la
        demanda por lo que si incrementas tus sesiones de amamantar o de sacarla con el tira leches
        esto puede ayudar a mejorar la producción y así hacer que el bebé reciba más leche al
        momento de comer.</p>
        <p><strong>Sube muy rápido de peso:</strong></p>        
        <p><strong>Puede estar pasando por un brote de crecimiento:</strong> como se mencionó anteriormente los
        bebés tienen 3 picos de crecimiento y si es durante este tiempo que sube mucho de peso no
        hay nada de qué preocuparse.</p>
        <p>Suplementas su alimentación con leche en formula: los bebés que comen exclusivamente
        leche materna no corren el riesgo de ser sobre alimentados pero los bebés que consumen
        leche en formula o son suplementados con ella puede llegar a ser un problema, pero aquí te
        damos unos tips para saber si tu bebé ya está satisfecho:</p>
