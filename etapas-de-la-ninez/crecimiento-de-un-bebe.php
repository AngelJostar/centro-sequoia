        <h3>Peso de un bebé al mes de nacido</h3>
        <p>Llegamos al primer mes de vida de tu bebé y estamos dejando atrás los las actividades de un
        recién nacido para empezar a conocer el <a href="">desarrollo de un bebé</a> (a primera infancia) con mayor
        fuerza e inteligencia. Para este momento los bebés ya empiezan a querer transmitir sus
        emociones a través de una sonrisa o imitando los gestos de mamá y papá.</p>
        <p>A la 5 semana de su nacimiento el bebé está pesando entre 4 y 5 kg, y puede estar dentro del
        90% y 10% del <a href="">percentil</a> de peso por lo que puede ser más grande o más chico a comparación
        del promedio. Recuerda no comparar a tu bebé con los de otras personas, su desarrollo y
        crecimiento es único y si tu doctor está satisfecho con sus medidas entonces tú relájate y
        observa con asombro la evolución de tu bebé.</p>
        <h3>Peso de un bebé a los dos meses de nacido</h3>
        <p>Para este momento en la vida de tu bebé ya te sientes más cómodo con tu nuevo rol de mamá
        o papá, tu bebé ya interactúa mucho más contigo y ya no se siente como un recién nacido.
        Durante este tiempo es importante empezar a poner al bebé boca abajo para que los músculos
        de su cuello y espalda empiecen a fortalecerse y pueda sostener su cabeza por sí solo.</p>
        <p>El peso promedio de un bebé de dos meses es de 5.15 kg y mide 57 cm para una niña y 5.5 kg
        y mide 58 cm para un niño. En este mes los bebés comen entre 4 y 6 onzas de leche cada 3 a
        4 horas.</p>
        <p>Durante esta etapa los bebés empiezan a crecer de forma muy rápida, pueden crecer 2.5 cm al
        mes y aumentar 110 a 225 gramos a la semana. Por lo que sentirás que cada mañana que
        despierte tu bebé es diferente y su ropa deja de quedarle rápidamente aunque solo la haya
        usado una o dos veces. Si tu bebé no está subiendo el peso necesario y lo alimentas con leche
        en formula puede ser que tengas que cambiar de marca y es mejor platicarlo con tu doctor para
        que él te indique cuál es la mejor opción para tu bebé.</p>
        <h3>Peso de un bebé a los tres meses</h3>
        <p>A los tres meses los bebés se vuelven muy alegres y los papás también, ya que, empiezan a
        dormir más horas durante la noche y se mueven más pero no son independientes aún por lo
        que todavía no hay mucho de que estarlos persiguiendo. En este mes tu bebé ya debe de
        poder sostener su cabeza gracias al tiempo que lo has puesto boca abajo, también empiezan a
        reírse, a voltearse solitos cuando estén acostados y los balbuceos que se convertirán en
        palabras empiezan a surgir en esta época.</p>
        <p>A los tres meses el promedio de un bebé niña es de 5.5 kg en su peso y de largo 61.4 cm,
        mientras que para un niño es de 6.3 kg y 59.8 cm. Para este punto <a href="">tu pediatra</a> ya debe de tener
        marcado su crecimiento en la <a href="">tabla de tu bebé</a>, por lo que aquí juntos verán si está aumentando
        los gramos y centímetros esperados para su edad. Es importante tener este record ya que los
        bebés que aumentan muy rápidamente de peso en los primeros meses de vida tienen un mayor
        <a href="">riesgo de obesidad</a> a los 3 años y si no se controla su peso puede impactarlos por el resto de
        su vida.</p>
        <h3>Peso de un bebé de cuatro meses</h3>
        <p>Durante este mes los bebés empiezan a experimentar con su personalidad y lo que más les
        gusta es socializar contigo o con quien sea que este cerca, así que prepárate para horas de
        diversión. Su cuerpo empieza a tener mayor fuerza y ya pueden levantar los brazos mientras
        están boca abajo y empiezan a poderse posicionar sentados.</p>
        <p>Las niñas en este mes deben de estar alrededor de los 4.5 y 8.6 kg midiendo entre 55 y 67.5
        cm, los niños pesan en promedio 4.9 y 9 kg y miden entre 58.7 y 68.5 cm. Los rangos son
        amplios y si tu doctor no está preocupado con su crecimiento tu tampoco deberías de estarlo,
        los bebés crecen y alcanzan los hitos de desarrollo en diferentes momentos por lo que no
        compares a tu bebé con el de los demás si no enfócate en darle a tu bebé lo mejor de ti.</p>
        <h3>Peso de un bebé de cinco meses</h3>
        <p>En este mes los bebés están alcanzando muchos momentos importantes, ahorita la visión y
        oído de tu bebé ya están desarrollados a un 100%, también empiezan a reconocer su propio
        nombre, ya se puede sostener en posición de lagartija cuando está en el piso y empiezan a
        usar sus juguetes con mayor precisión.</p>
        <p>Algo importante durante este mes y el siguiente es considerar introducir alimentos sólidos pero
        antes de hacerlo hay que consultarlo con el pediatra. Por este posible cambio en la alimentación de los bebés pueden empezar a subir de peso de manera importante por lo que
        en promedio las niñas pesan 6.9 kg y miden 64 cm mientras los niños pesan 7.5 kg y miden
        65.9 cm.</p>
        <p>Aunque se introduzcan sólidos no esperes que coma demasiado en las primeras probadas, se
        les puede dar 1 cucharadita de puré para bebé dos veces al día, ahorita es más importante que
        el bebé agarre experiencia y checar si es alérgico a alguna comida más que la parte nutricional.
        La leche materna o formula seguirá siendo la principal fuente de nutrición de tu bebé.</p>
        <h3>Peso de un bebé de seis meses</h3>
        <p>Ya vamos a la mitad de su primer año de vida, es un momento emocionante para ti y tu bebé. A
        partir de ahora empieza la cuenta regresiva para que tu bebé empiece a gatear y caminar solito
        por lo que disfruta lo que queda de tenerlo cargado en tus brazos sin que se queje.</p>
        <p>Ahorita tendrá muchos <a href="">eventos importantes en su desarrollo</a> y uno de los más importantes es
        poderse sentar ya sin apoyo por lo que ya lo podrás sentar en su silla de comer para empezar
        con la experiencia de comer sólidos. Algunos bebés ya pueden soportar su propio peso con el
        apoyo de mamá y papá, por lo que su fortaleza incrementa con cada mes que pase y es
        increíble verlo lograr más por sí solo.</p>
        <p>Un bebé de seis meses con la combinación de sólidos y leche puede pesar en promedio para
        los niños entre 6 y 13 kg y medir entre 60 y 73 cm. Las niñas en promedio pesan entre 5.8 y 10
        kg y medir entre 58 y 71 cm. Al alimentar a tu bebé para apoyar en su desarrollo recuerda
        introducir alimentos poco a poco, puede ser uno diferente cada día para así notar cualquier
        intolerancia o alergia que pueda tener y revisar con tu médico cuáles comidas están prohibidas
        para bebés de esta edad.</p>
        <h3>Peso de un bebé de siete meses</h3>
        <p>En este mes prepárate para todo lo que tu bebé va a querer explorar, si para este mes no se ha
        podido sentar solo ahorita puede ya ser momento. Sigue poniendo a tu bebé boca abajo esto
        estimula el gatear y los músculos necesarios para un día poder caminar, mientras más lo
        promueves más rápido lograran gatear y caminar. También es posible que empieces a ver
        signos de su primer diente.</p>
        <p>Este mes sentirás que tu bebé crece mucho y probablemente sea verdad, ahorita pueden
        aumentar 500 gramos y 1.25 cm de largo este mes. En las tablas de crecimiento de tu bebé
        ahora podrás notar una curva propia y así tener mejor entendimiento del desarrollo de tu bebé.
        Ahora que tu bebé es un comedor más experimentado puedes empezar a darle más comidas
        solidas en las diferentes comidas para así fomentar y mantener su crecimiento.</p>
        <h3>Peso de un bebé de ocho meses</h3>
        <p>Tu bebé está muy ocupado explorando y aprendiendo todo lo que puede, es ahora que puede
        empezar a usar más sus habilidades de comunicación. Empiezan a usar ruidos y gruñidos para
        dar a entender lo que quieren, también pueden empezar a señalar con el dedo. El bebé tan
        social que parecías tener en meses anteriores ahora puede resultar miedoso ante extraños,
        esto no es un tema de personalidad ni de qué preocuparse ya que esto refleja madurez en él y
        se le pasará pronto pero por el momento puede parecer penoso con los demás.</p>
        <p>El peso de una niña bebé ahora puede estar entre los 5.8 y 10.5 kg y un niño bebé entre 6.3 y
        11.3 kg, y miden entre 63.5 y 76 cm. Para mantener su crecimiento es momento de empezar a
        dejar que tu bebé pruebe comidas que pueda agarrar entre sus dedos, es un momento
        importante porque se da cuenta que ella se puede alimentar sola pero recuerda siempre
        consultar cualquier cambio en la alimentación con tu pediatra. Esta habilidad es un hito
        importante en su progreso y es cuando desarrollan el agarre de pinza, habilidad esencial para
        los seres humanos.</p>
        <h3>Peso de un bebé de nueve meses</h3>
        <p>Los ruidos que llevabas escuchando antes ahora se están convirtiendo en palabras reales, en
        este mes tu bebé puede estar cerca de decir “mamá” y “papá”, un momento que te llenara de
        emoción y alegría pero si no pasa durante este mes no te frustres sigue hablándole a tu bebé
        para así estimular el habla. También puede ser el momento en el que aprenda a gatear, no
        importa si tu bebé lo hace antes o después de este mes ya que seguramente está cerca de
        poderse trasladar por si solo así que recuerda adaptar tu hogar para un bebé en movimiento.</p>
        <p>El crecimiento del bebé en este mes estará rodeando los 8.2 kg para las niñas y 8.9 kg para los
        niños, su largo es alrededor de los 72 cm para los niños y 70.1 para las niñas. Es probable que
        en este mes que su apetito incremente por lo que puede ser buena idea empezar a darle
        pequeños bocaditos a lo largo del día a parte de sus comidas importantes, puedes darle
        verduras y frutas para así empezar a promover el comer sanamente desde temprana edad.</p>
        <h3>Peso de un bebé de diez meses</h3>
        <p>En este mes tu bebé puede parecer un pequeño torbellino con la cantidad de actividad que
        estará queriendo realizar, ya al gatear se trasladan de forma fácil a los lugares que les
        interesan pero también pueden empezar a pararse por si solos con apoyos de mesas o sillas
        por lo que es importante estarlos vigilando constantemente para evitar cualquier tipo de
        accidente. Estarás esperando la hora de dormir con ansias gracias a lo cansado que es tras él
        todo el día.</p>
        <p>En este mes el crecimiento de tu bebé empieza a estabilizarse y podrás notar que sus medidas
        no son tan dramáticamente diferentes al mes anterior como lo habían estado siendo. El peso
        de una bebé durante este mes es de 8.5 kg y de un niño de 9.2 kg, midiendo respectivamente
        71.5 cm y 73.3 cm.</p>
        <h3>Peso de un bebé de once meses</h3>
        <p>Este es el último mes del primer año de tu bebé y puedes estar pensando en lo rápido que
        pasaron y que parece más niño cada día. Durante este mes y el siguiente puedes empezar a
        notar rasgos de su personalidad que mandaran los siguientes 3 años, también puede algunos bebés que estén empezando a caminar por si solos (lo normal es hasta que cumplan el año)
        por lo que es recomendable darles juguetes que puedan empujar o andaderas que le
        promuevan el movimiento de caminar ya que esto apoyara el desarrollo de sus músculos y
        huesos.</p>
        <p>El peso de un bebé niño durante este mes es de 9 y 12 kg y mide 81 cm, para las niñas el peso
        puede ser entre 8.6 y 11.3 kg y miden en promedio 78.7 cm. En este mes necesitan mucha
        más proteína y carbohidratos para soportar el nivel de movilidad que tienen por lo que pueden
        estar necesitando más nutrientes que los que reciben de la leche en formula o leche materna,
        por lo que recomendamos platicar con tu doctor sobre la dieta que debe empezar a llevar tu
        bebé ahora que se acerca a su primer cumpleaños.</p>
        <h3>Peso de un bebé de doce meses/1 año</h3>
        <p>¡Tu bebé tiene un año! Es difícil entender cómo pudo pasar un año tan rápido y ahora tu bebé
        ya es más un niño que puede hacer cosas sin tu ayuda y empieza a convertirse en un ser
        independiente. Sigue estimulando su desarrollo con diferentes juegos y juguetes, preséntale las
        crayolas y que empiece a explorar su creatividad o un instrumento y tal vez descubres un
        talento escondido o solo le gustará hacer ruido y será un dolor de cabeza. Pero es momento de
        que tu bebé pruebe todo lo que sea posible para que su desarrollo físico y mental.</p>
        <p>El peso promedio de un bebé niño es 9.6 kg y mide 75.7 cm y una niña pesa 8.9 kg y mide 74
        cm. Después de su primer cumpleaños es normal que su crecimiento ya no sea tan dramático
        como venía siendo pero mientras tu doctor siga midiendo su desarrollo en su tabla y la curva
        sea normal o que no le parezca preocupante te puedes quedar tranquila. Amamantar es
        recomendado el primer año de vida de los bebés por lo que puedes estar llegando a la
        conclusión de esta etapa o tal vez ya lo habías dejado pero tu bebé sigue necesitando los
        nutrientes que le daba la leche por lo que platica con tu doctor para saber cómo debe ser la
        dieta de tu bebé ahora que se eliminó la leche materna o de formula.</p>
        <p>Llegamos al final del primer año de vida de tu bebé por lo que esperamos que esta guía te haya ayudado a entender el crecimiento que sucede en los primeros doce meses de tu hijo. Es importante recordar que todos los bebés crecen y se desarrollan de forma diferente, si te podemos dejar con un consejo final es confiar en tu doctor y no comparar a tu bebé con los demás, disfruta cada día y cada nuevo logro porque es un año que pasa muy rápido y que se regresará jamás.</p>
