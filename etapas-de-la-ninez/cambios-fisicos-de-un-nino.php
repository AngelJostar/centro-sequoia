        <p>El <a href="/etapas-de-la-ninez">desarrollo físico</a> del niño se lleva a cabo de forma progresiva, pero puede verse influida tanto de forma positiva como de forma negativa por factores ambientales y de experiencia. Algunos de ellos son: </p>
        <ul>
            <li><strong>Atención prenatal:</strong> la falta de atención prenatal o una exposición en el periodo prenatal a sustancias como drogas y alcohol influyen negativamente en el desarrollo.</li>
            <li><strong>Necesidades básicas:</strong> Si los padres no satisfacen las necesidades básicas del niño como la seguridad, el amor, la vivienda y comida esto puede influir de forma negativa en el desarrollo del cerebro, lo que a su vez afecta el desarrollo físico del niño.</li>
            <li><strong>Cultura:</strong> Existen lugares en dónde por cultura no se incentiva la independencia y el apoyo al desarrollo físico del niño. </li>
            <li>Retrasos en el cambio físico del niño, discapacidades o problemas de salud.</li>
        </ul>
